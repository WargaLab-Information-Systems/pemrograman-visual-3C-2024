    private void btncekActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btncekActionPerformed
        // TODO add your handling code here:
        try {
        int tebak = Integer.parseInt(tebakan.getText());
        if (tebak < 1 || tebak > 100) {
            JOptionPane.showMessageDialog(this, "Masukkan angka antara 1-100");
            return;
        }

        kesempatan--;
        btncek.setText(" Cek (" + kesempatan + ")");

        if (tebak < target) {
            cek.setText("Lebih besar");
        } else if (tebak > target) {
            cek.setText("Lebih kecil");
        } else {
            score = (kesempatan  + 1) * 10;
            scor.setText(""+ score);
            cek.setText("Berhasil! Aku adalah angka " + tebak);
            String name = null;

            while (name == null || name.trim().isEmpty()) {
                name = JOptionPane.showInputDialog("Masukkan nama Anda:");

                if (name == null || name.trim().isEmpty()) {
                    JOptionPane.showMessageDialog(this, "Nama tidak boleh kosong. Silakan masukkan nama!");
                }
            }

            listscore.add(name + " Mendapatkan Skor " + score); 
            listscore.repaint();  
            JOptionPane.showMessageDialog(this, "Selamat " + name + "! Skor Anda: " + score);
            resetGame();
            return;
        }

        if (kesempatan == 0) {
            JOptionPane.showMessageDialog(this, "Anda Gagal! Angka yang benar adalah " + target);
            resetGame();
        }

        scor.setText(String.valueOf(score));
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "Masukkan angka yang valid");
    }
    }//GEN-LAST:event_btncekActionPerformed

