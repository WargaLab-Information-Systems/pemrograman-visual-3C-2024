    private void btnTebakActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTebakActionPerformed
        // TODO add your handling code here:
        try{
            int tebakan = Integer.parseInt(colTebak.getText());
            percobaan ++;
            System.out.println(""+angkaTebakan);
            
            if (tebakan <=0 || tebakan > 100){
                playSFXinvalid();
                JOptionPane.showMessageDialog(this, null ,"Astagfirullah tidak valid bro", JOptionPane.WARNING_MESSAGE,iconInvalid);
            }else if(tebakan < angkaTebakan){
                colHint.setText("Masih terlalu rendah");
                playSFXsalah();
                JOptionPane.showMessageDialog(this, "Awowkwowowko terlalu rendah" ,"Terlaku Rendah hahaha", JOptionPane.WARNING_MESSAGE,iconSalah);
                
            }else if(tebakan > angkaTebakan){
                colHint.setText("Tebakan Ketinggian");
                playSFXsalah();
                JOptionPane.showMessageDialog(this, "Awowkwowowko ketinggian" ,"Terlalu Tinggi hahaha", JOptionPane.WARNING_MESSAGE,iconSalah);
            }
            else {
                int totalScore = (maksPercobaan-percobaan)*10;
                colScore.setText(""+totalScore);
                colHint.setText("Anda Berhasil!");
                playSFXbenar();
                parti();
                JOptionPane.showMessageDialog(this, "\nSelamat Anda Benar !!!\nIQ +80 \nAngka Tebakan : " +angkaTebakan,"SELAMAT ORANG MUSLIM", JOptionPane.INFORMATION_MESSAGE,iconBerhasil);
                String name = JOptionPane.showInputDialog(this, "Masukkan nama Anda: ");
               
                if(name == null){
                    JOptionPane.showMessageDialog(this,"Input dibatalkan", "Input Canceled",JOptionPane.INFORMATION_MESSAGE,car);
                    resetGame();
                }else if(name.trim().isEmpty()){
                    JOptionPane.showMessageDialog(this,"Nama Anda Kosong", "Input Tidak Valid",JOptionPane.INFORMATION_MESSAGE,car);
                    resetGame();
                }else{
                    JOptionPane.showMessageDialog(this, "BAIKLAH AKHI : " +name.toUpperCase()+" HASIL TEST ANDA \nSUDAH KAMI CATAT","SIP SAYA CATAT !!!", JOptionPane.INFORMATION_MESSAGE,iconBerhasil);
                     listScoreModel.addElement((nomorList++)+". "+name+ " - Skor: " + totalScore);
                     resetGame();
                }
                return;
            }
             if (percobaan >= maksPercobaan) {
                JOptionPane.showMessageDialog(this, "Anda Gagal! Target angka adalah: " + angkaTebakan);
                resetGame();
            } else {
                btnTebak.setText("Guess ( "+((maksPercobaan)-percobaan)+" )");
            }

            
        }catch(NumberFormatException ex){
            percobaan ++;
            int scoreAwal = Integer.parseInt(colScore.getText());
            System.out.println("cihuy");
            scoreAwal -= 10;
            colScore.setText(""+scoreAwal);
            playSFXinvalid();
            JOptionPane.showMessageDialog(this, null ,"Tidak Valid coiiiii", JOptionPane.WARNING_MESSAGE,iconInvalid);
            btnTebak.setText("Guess ( "+((maksPercobaan)-percobaan)+" )");
            
        }
        
    }//GEN-LAST:event_btnTebakActionPerformed

