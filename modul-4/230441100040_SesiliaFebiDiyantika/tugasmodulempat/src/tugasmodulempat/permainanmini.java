    private void bttebakActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bttebakActionPerformed
        // TODO add your handling code here:
        try{
            int angka2 = Integer.parseInt(txtcek.getText());
            if (tebakan > angka2)
            {
                kesempatan -=1;
                bttebak.setText("TEBAK ("+kesempatan+")");
                txttebak.setText("Angka Tebakan Lebih Besar");
                if(kesempatan == 0){
                    kesempatan = 10;
                    bttebak.setText("Tebak ("+kesempatan+")");
                    JOptionPane.showMessageDialog(this, "EH YAAMPUN KESEMPATAN HABIS", "PESAN PERINGATAN", JOptionPane.ERROR_MESSAGE);
                    txtcek.setText("");
                    txttebak.setText("");
                    txtscore.setText("");
                    bttebak.setText("TEBAK(10)");
                    kesempatan = 10;
                }
            }else if(tebakan < angka2)
            {
                txttebak.setText("Angka Tebakan Lebih Kecil");
                kesempatan -=1;
                bttebak.setText("TEBAK ("+kesempatan+")");
                if (kesempatan == 0){
                    kesempatan = 10;
                    bttebak.setText("Tebak ("+kesempatan+")");
                    JOptionPane.showMessageDialog(this, "EH YAAMPUUN KESEMPATAN HABIS", "INFORMASI", JOptionPane.ERROR_MESSAGE);
                    txtcek.setText("");
                    txttebak.setText("");
                    txtscore.setText("");
                    bttebak.setText("TEBAK(10)");
                    kesempatan = 10;
                }
            }else if (tebakan == angka2)
            {
                txttebak.setText(String.valueOf(tebakan));
                int hasil = kesempatan * 10;
                scorefix.setText("");
                txtscore.setText(String.valueOf(hasil));
                JOptionPane.showMessageDialog(this, "SELAMAT TEBAKAN ANDA BENAR", "INFORMASI", JOptionPane.INFORMATION_MESSAGE);
                String nama = JOptionPane.showInputDialog(this, "SILAHKAN MASUKKAN NAMA", "Nama player", JOptionPane.INFORMATION_MESSAGE);
                if (index < data_score.length){
                    data_score[index][0] = nama;
                    data_score[index][1] = String.valueOf(hasil);
                    index++;
                }
                
                txttebak.setText("");
                txtscore.setText("");
                bttebak.setText("TEBAK(10)");
                kesempatan = 10;
                score_border.setLength(0); //agar tidak double
                
                for (int i = 0; i < index; i++){
                score_border.append("Nama: ").append(data_score[i][0]).append(", Score: ").append(data_score[i][1]).append("\n");
            }
                scorefix.setText(score_border.toString());
            }
        }catch (NumberFormatException e){
                    JOptionPane.showMessageDialog(this, "TEBAKAN HARUS BERUPA ANGKA", "PESAN PERINGATAN", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_bttebakActionPerformed

