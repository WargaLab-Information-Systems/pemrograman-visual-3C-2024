private void resetGame() {
    target = (int) (Math.random() * 100) + 1; // Mengganti angka target secara acak
    attempts = 0; // Mengatur ulang jumlah percobaan
    angka.setText(""); // Mengosongkan input tebakan
    cek.setText(""); // Mengosongkan pesan hasil tebakan
    score.setText("Score: 0"); // Mengatur ulang skor menjadi 0
    updateTebakButton(); // Perbarui teks tombol Tebak untuk menampilkan kesempatan yang tersisa
    }

private void tebakAngka() {
        try {
            int tebakan = Integer.parseInt(angka.getText());
            attempts++;

            if (tebakan < 1 || tebakan > 100) {
                cek.setText("Angka harus 1-100!");
            } else if (tebakan > target) {
                cek.setText("Aku adalah angka lebih kecil!");
            } else if (tebakan < target) {
                cek.setText("Aku adalah angka lebih besar!");
            } else {
                cek.setText("Tebakan Benar! Anda Menang!");

                // Hitung skor berdasarkan kesempatan yang tersisa
                int scoreVal = (maxAttempts - attempts + 1) * 10;
                score.setText("Score: " + scoreVal);

                // Minta nama setelah berhasil
                String nama = JOptionPane.showInputDialog(this, "Selamat Tebakan Anda Benar! Masukkan Nama Anda:");
                if (nama != null && !nama.isEmpty()) {
                    scoreListModel.addElement(nama + " - Score: " + scoreVal);
                }
                resetGame();
                return;
            }

            updateTebakButton();

            if (attempts >= maxAttempts) {
                JOptionPane.showMessageDialog(this, "Anda Gagal! Angka yang benar: " + target);
                resetGame();
            }
        } catch (NumberFormatException ex) {
            cek.setText("Masukkan angka yang valid!");
        }
    }

    private void updateTebakButton() {
        int remainingAttempts = maxAttempts - attempts;
        tebak.setText("Tebak (" + remainingAttempts + ")");
    }


