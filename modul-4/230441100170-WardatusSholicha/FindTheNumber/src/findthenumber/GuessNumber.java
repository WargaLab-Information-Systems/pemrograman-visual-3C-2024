    private void btnResetActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnResetActionPerformed
    if (kesempatan == 0) { 
        kesempatanHabis();
        tfAngkaTebak.setText("");
        tfAngkaAsli.setText("");
      
        JOptionPane.showMessageDialog(this, "Permainan telah direset!" + "\n"
                + "Silahkan Mulai Permainan Baru.");
        } else {
        JOptionPane.showMessageDialog(this, "Kesempatan Anda masih tersisa! Lanjutkan permainan!", "Info", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_btnResetActionPerformed

    private void btnTebakActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTebakActionPerformed
        try {
            int tebakan = Integer.parseInt(tfAngkaTebak.getText());

            if(tebakan < 0 || tebakan > 100){
                JOptionPane.showMessageDialog(this, "Masukkan angka antara 0 dan 100!", "Error", JOptionPane.ERROR_MESSAGE);
            }

            if (tebakan == targetAngka) {
                score = kesempatan* 10;
                tfScore.setText("Score: " + score);
                String nama = JOptionPane.showInputDialog(this, "Anda Berhasil! Masukkan nama Anda:");
                scoreModel.addElement(nama + " - Score: " + score);
                kesempatanHabis();
            } else {
                kesempatan--;
                score = kesempatan * 10;
                tfScore.setText("Score: " + score);
                btnTebak.setText("Tebak (" + kesempatan+ ")");

                if (kesempatan == 0) {
                    JOptionPane.showMessageDialog(this, "Anda Gagal! Angka yang Benar adalah :" +targetAngka +"\n" + "Permainan direset.");
                    kesempatanHabis();
                } else if (tebakan < targetAngka) {
                    tfAngkaAsli.setText("Angka Lebih besar!");
                } else {
                    tfAngkaAsli.setText("Angka Lebih kecil!");
                }
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Masukkan angka yang valid!", "Error", JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_btnTebakActionPerformed

