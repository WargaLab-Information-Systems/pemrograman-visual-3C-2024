    private void kesempatanHabis() {
        Random rand = new Random();
        targetAngka = rand.nextInt(100) + 1; 
        kesempatan= 10; 
        score = 0; 
        btnTebak.setText("Tebak (" + kesempatan + ")");
        tfScore.setText("Score: " + score);
        tfAngkaTebak.setText(""); 
        tfAngkaAsli.setText(""); 
      }  

