    private void btntebakActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btntebakActionPerformed
        // Proses tebakan jika masih ada sisa percobaan
        if (attempts > 0) {
            try {
                int guess = Integer.parseInt(tfinputtebak.getText()); //mengonversi teks menjadi bilangan bulat.
                attempts--; //mengurangi jumlah kesempatan yg tersisa untk pemain
                PerbaruiTebakan();
                
                if (guess < target) {
                    tfcek.setText("Tebakan terlalu kecil!");
                } else if (guess > target) {
                    tfcek.setText("Tebakan terlalu besar!");
                } else {
                    tfcek.setText("Anda Berhasil! Target: " + target);
                    String name = JOptionPane.showInputDialog("Masukkan Nama Anda:");
                        if (name != null && !name.isEmpty()) { //Memeriksa apakah nama yang dimasukkan oleh pemain tidak null
                        int score = attempts * 10; //score sama dengan di x 10 (sisa kesempatan)
                        scoreListModel.addElement(name + ": " + score); // Tambahkan skor ke model
}
                    btnreset.setText("RISET");
                    btntebak.setEnabled(false);
                }
            } catch (NumberFormatException ex) {
                JOptionPane.showMessageDialog(null, "Masukkan Angka Yang Valid", "PERINGATAN", JOptionPane.WARNING_MESSAGE);
            }
        }
        
        // Kondisi jika percobaan habis dan gagal
        if (attempts == 0 && btntebak.isEnabled()) {
        tfinputtebak.setText("Anda Gagal! Target: " + target);
        btnreset.setText("RESET");
        btntebak.setEnabled(false);
        }
    }//GEN-LAST:event_btntebakActionPerformed

   
