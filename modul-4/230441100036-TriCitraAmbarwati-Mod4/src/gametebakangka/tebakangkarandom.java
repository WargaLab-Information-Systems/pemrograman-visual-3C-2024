    private void BtnTebakActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnTebakActionPerformed
        // TODO add your handling code here:
        try {
            int tebakan = Integer.parseInt(CobaTebak.getText());
            Peluang--;

            if (tebakan < AngkaTargetan) {
                Cek.setText("Angka Anda Terlalu Kecil!!!");
            } else if (tebakan > AngkaTargetan) {
                Cek.setText("Angka Anda Terlalu Besar!!!");
            } else {
                JOptionPane.showMessageDialog(this, "Anda Telah Berhasil");
                String nama = JOptionPane.showInputDialog(this, "Silahkan Masukkan Nama Anda:");
                int score = (Peluang + 1) * 10;
                String skornya = nama + " - Score : " + score;
                ScoreModel.addElement(skornya);
                Score.setText("" + score);
                Cek.setText("Aku Adalah Angka : " + AngkaTargetan);
                ResetGames();
            }

            BtnTebak.setText("Tebak(" + Peluang + ")");
            
            if (Peluang <= 0) {
                JOptionPane.showMessageDialog(this, "Anda Telah Gagal, Dan Kesempatan Anda Telah Habis.");
                Score.setText("0");
                ResetGames();
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Masukkan angka yang valid!", "Error", JOptionPane.ERROR_MESSAGE);
            CobaTebak.setText("");
        }
    }//GEN-LAST:event_BtnTebakActionPerformed

