    private void btn_checkActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_checkActionPerformed
        try {
            int guess = Integer.parseInt(tf_guess.getText());
            if (guess == num) {
                int score = count * 10;
                tf_info.setText("yeaahhh, you're right!");
                tf_score.setText(String.valueOf(score));
                String name = JOptionPane.showInputDialog(this, "you did it! what's your name, buddy?");
                nilai.add(name + " - Score: " + score); 
                tf_score.setText("");
                update();

                num = random.nextInt(100) + 1;
                count = 10;
                tf_guess.setText("");
                JOptionPane.showMessageDialog(this, "game reset, come on guess me again><");
                tf_info.setText("");
                btn_check.setText("CHECK (" + count + ")"); 
            } else if (guess < num) {
                count--;
                tf_info.setText("your guess is too small!");
                tf_guess.setText("");
            } else {
                count--;
                tf_info.setText("your guess is too big!");
                tf_guess.setText("");
            }
            
            if (count <= 0) { 
                JOptionPane.showMessageDialog(this, "you failed, chances are gone, see you again!");
                
                num = random.nextInt(100) + 1;
                count = 10;
                tf_guess.setText("");
                tf_info.setText("");
                btn_check.setText("CHECK (" + count + ")"); 
            } else {
                btn_check.setText("CHECK (" + count + ")"); 
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "enter a valid number buddy!"); 
        }
    }//GEN-LAST:event_btn_checkActionPerformed

