    private void tomboltebakActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tomboltebakActionPerformed
        // Tombol tebak
        try{
            int angka = Integer.parseInt(txttebak.getText());
            if (tebakan > angka)   
            {
                kesempatan -=1;
                System.out.println(tebakan);
                tomboltebak.setText("Tebak ("+kesempatan+")");
                txtcek.setText("angka terlalu kecil, coba tebak angka besar");
                if (kesempatan == 0){
                    kesempatan = 10;
                    tomboltebak.setText("Tebak ("+kesempatan+")");
                    JOptionPane.showMessageDialog(this, "YAH KESEMPATAN UDAH HABIS:("); 
                    txttebak.setText("");
                    txtcek.setText("");
                    txtscore.setText("0");
                    tomboltebak.setText("Tebak(10)");
            }//=kondisi
             //==false
            }else if (tebakan < angka)
            {
                kesempatan -=1;
                tomboltebak.setText("Tebak ("+kesempatan+")");
                txtcek.setText("angka terlalu besar, coba tebak angka kecil");
                if (kesempatan == 0){
                    kesempatan = 10;
                    tomboltebak.setText("Tebak ("+kesempatan+")");
                    JOptionPane.showMessageDialog(this, "YAH KESEMPATAN UDAH HABIS:(");
                    txttebak.setText("");
                    txtcek.setText("");
                    txtscore.setText("0");
                    tomboltebak.setText("Tebak(10)");
                }   
            }else if (tebakan == angka)
            {
                txtcek.setText(String.valueOf(tebakan));
                int hasil = kesempatan * 10;
                txttotalscore.setText("");
                txtscore.setText(String.valueOf(hasil));
                JOptionPane.showMessageDialog(this, "YEYYY TEBAKANMU BENAR!");
                String nama = JOptionPane.showInputDialog(this, "MASUKIN NAMA DULU DONG:P");
                    if (index < data_score.length){//mengitung panjang
                            data_score[index][0]= nama;
                            data_score[index][1]= String.valueOf(hasil);
                            index++; //Increment index setelah data ditambahkan
                    }
                   
                    //ke riset sendiri
                    txttebak.setText("");
                    txtcek.setText("");
                    txtscore.setText("0");
                    tomboltebak.setText("Tebak(10)");
                    
                    score_border.setLength(0);//frbl
                    
                    //
                    for (int i = 0; i < index; i++){
                            score_border.append("Name: ").append(data_score[i][0]).append(", Score: ").append(data_score[i][1]).append("\n");
                    //append agar list tidak double
                    }   
                    txttotalscore.setText(score_border.toString());//pengubhn dari angka ke string
            }
        }catch(NumberFormatException e){//format hrs angka
            JOptionPane.showMessageDialog(this, "KLO MAU NEBAK HARUS PAKE ANGKA YAA");
        }
    }//GEN-LAST:event_tomboltebakActionPerformed

