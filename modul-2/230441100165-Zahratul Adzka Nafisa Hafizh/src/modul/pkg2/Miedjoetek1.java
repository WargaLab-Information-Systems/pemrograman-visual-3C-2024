    private void txtcashKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtcashKeyReleased
        // TODO add your handling code here:
        try {
        double total = Double.parseDouble(txttotal.getText().replace("Rp. ", ""));
        int bayar = Integer.parseInt(txtcash.getText());
        if(bayar<total){
            txtkembalian.setText("Uang Kurang");
        }else{
        double kembalian = bayar - total;
        txtkembalian.setText("Rp. "+String.valueOf(kembalian));
        }
        } catch (NumberFormatException ex) {
        txtcash.setText("");
        JOptionPane.showMessageDialog(this, "Silhakan Masukan Angka");
        }
//        if (txtcash.getText() == null) {
//            JOptionPane.showMessageDialog(null, "Nominal jangan Kosong!", "Peringatan", JOptionPane.WARNING_MESSAGE);
//        } 
        double hargasetelahdiskon = Double.parseDouble(txthargasetelahdiskon.getText());
        double uangmasuk = Double.parseDouble(txtcash.getText());
        double kembalian = uangmasuk - hargasetelahdiskon;
        
        if (kembalian >= 0) {
                txtkembalian.setText(String.valueOf(kembalian));
        } else {
            txtkembalian.setText("Uang Tidak Cukup");
        }
    }//GEN-LAST:event_txtcashKeyReleased

