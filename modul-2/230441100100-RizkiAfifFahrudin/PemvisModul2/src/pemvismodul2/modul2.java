    private int jumlahPesanan = 0;
    
    private void btnPlusMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnPlusMouseClicked

        jumlahPesanan++;
        
        teksJumlah.setText(String.valueOf(jumlahPesanan));
    }//GEN-LAST:event_btnPlusMouseClicked

    private void btnMinMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnMinMouseClicked
     
        if(jumlahPesanan>0){
        jumlahPesanan--;
        
        teksJumlah.setText(String.valueOf(jumlahPesanan));
        }else{
        JOptionPane.showMessageDialog(null,"Tidak Usah Beli","Peringatan",JOptionPane.WARNING_MESSAGE);
        }       
    }//GEN-LAST:event_btnMinMouseClicked

    private void btnPlus10MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnPlus10MouseClicked

        jumlahPesanan+=10;
        
        teksJumlah.setText(String.valueOf(jumlahPesanan));
    }//GEN-LAST:event_btnPlus10MouseClicked

    private void btnMin10MousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnMin10MousePressed

        if(jumlahPesanan>0 && jumlahPesanan<10){
            jumlahPesanan = 0;
            teksJumlah.setText(String.valueOf(jumlahPesanan));
        }else if(jumlahPesanan>=10){
            jumlahPesanan -= 10;
            teksJumlah.setText(String.valueOf(jumlahPesanan));
        }else{
            JOptionPane.showMessageDialog(null,"Tidak Usah Beli","Peringatan",JOptionPane.WARNING_MESSAGE);
            }      
    }//GEN-LAST:event_btnMin10MousePressed

