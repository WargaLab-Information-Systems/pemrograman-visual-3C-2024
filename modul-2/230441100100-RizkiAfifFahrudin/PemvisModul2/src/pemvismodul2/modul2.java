    private void btnHitungActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHitungActionPerformed
        
    if(btnMenuRoti.getSelection() == null){
    JOptionPane.showMessageDialog(null, "Menu roti harus dipilih");
    return;
    }

    if(btnTopping.getSelection() == null){
    JOptionPane.showMessageDialog(null, "Topping harus dipilih");
    return;
    }
    
    if(teksJumlah.getText().isEmpty()){
    JOptionPane.showMessageDialog(null, "Jumlah harus diisi");
    return;
    }else if(Integer.parseInt(teksJumlah.getText())== 0){
    JOptionPane.showMessageDialog(null, "Isi yang benar Wakkk");
    return;
    }

    int totalHarga = 0;
    int harga = 0;
    int topping = 0;
    int jumlah = Integer.parseInt(teksJumlah.getText());

    if(btnNoTopping.isSelected()) {
    topping += 0;
    }else if(btnStrawberry.isSelected()){
    topping += 3000;
    }else if(btnCoklat.isSelected()) {
    topping += 2000;
    }else if(btnKacang.isSelected()) {
    topping += 5000;
    }

    if(btnRotiO.isSelected()) {
    harga += 20000;
    }else if(btnRotiChanai.isSelected()) {
    harga += 30000;
    }else if(btnRotiJala.isSelected()) {
    harga += 15000;
    }else if(btnRotiAwoka.isSelected()) {
    harga += 6000;
    }else if(btnRotiSisir.isSelected()) {
    harga += 10000;
    }else if(btnRotiSari.isSelected()) {
    harga += 100000;
    }else if(btnRotiTawar.isSelected()) {
    harga += 12000;
    }else if(btnRotiJumbo.isSelected()) {
    harga += 25000;
    }

    totalHarga += (harga + topping) * jumlah;
    
    if(totalHarga>=100000){
    double diskon = 0.05 * totalHarga;
    totalHarga -= diskon;
    }

    teksTotal.setText(String.valueOf(totalHarga));
    }//GEN-LAST:event_btnHitungActionPerformed

