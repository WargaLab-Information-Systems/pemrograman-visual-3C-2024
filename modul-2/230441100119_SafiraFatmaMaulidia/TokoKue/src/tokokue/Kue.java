    private void btntotalActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btntotalActionPerformed
    btntotal.addActionListener(e -> {
    if (bgkue.getSelection() == null || bgtopping.getSelection() == null) {
        JOptionPane.showMessageDialog(null, "Silakan pilih kue dan topping terlebih dahulu!");
        return;
    }
    
    if (matcha.isSelected()) {
        totalHarga = 35000;
    } else if (rv.isSelected()) {
        totalHarga = 38000;
    } else if (chocolate.isSelected()) {
        totalHarga = 40000;
    } else if (cheesecake.isSelected()) {
        totalHarga = 40000;
    } else if (tiramisu.isSelected()) {
        totalHarga = 39000;
    } else if (lotus.isSelected()) {
        totalHarga = 37000;
    }

    if (original.isSelected()) {
        totalHarga += 0;
    } else if (cherry.isSelected()) {
        totalHarga += 5000;
    } else if (keju.isSelected()) {
        totalHarga += 4000;
    } else if (oreo.isSelected()) {
        totalHarga += 3000;
    } else if (tpchocolate.isSelected()) {
        totalHarga += 5000;
    } else if (strawberry.isSelected()) {
        totalHarga += 6000;
    }

    totalHarga *= jumlahPesanan;

    if (totalHarga > 100000) {
        totalHarga *= 0.95;
    }

    total.setText(String.valueOf(totalHarga));
});
    }//GEN-LAST:event_btntotalActionPerformed

