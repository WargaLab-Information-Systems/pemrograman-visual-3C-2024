    private void btnTotalActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTotalActionPerformed
    if(gBread.getSelection()==null){
       JOptionPane.showMessageDialog(this, "Please, select any of the bread type", "Don't forget to add topping if u would to", JOptionPane.INFORMATION_MESSAGE);
       return;
    }else if(Bqty == 0){
        JOptionPane.showMessageDialog(this,"Please, input how many bread with/without toppings would you want","DISCLAIMER !!!!", JOptionPane.INFORMATION_MESSAGE);
        tfTotalqty.setText("");
    }
    
     double price = 0;

    if(btnWhite.isSelected()){
        price += 3.50;
    }
    if(btnGrain.isSelected()){
        price += 3.00;
    }
    if(btnWheat.isSelected()){
        price += 3.75;
    }
    if(btnPretzel.isSelected()){
        price += 4.00;
    }
    if(btnCroissant.isSelected()){
        price += 3.75;
    }
    if(btnBaguette.isSelected()){
        price += 4.00;
    }
    if(btnBagels.isSelected()){
        price += 4.50;
    }
    if(btnSticks.isSelected()){
        price += 3.50;
    }
    if(btnChoco.isSelected()){
        price += 1.99;
    }
    if(btnCheese.isSelected()){
        price += 1.99;
    }
    if(btnCaramel.isSelected()){
        price += 2.25;
    }
    if(btnBischoff.isSelected()){
        price += 2.50;
    }
    
    if (!btnChoco.isSelected() && !btnCheese.isSelected() && !btnCaramel.isSelected() && !btnBischoff.isSelected()) {
    int response = JOptionPane.showConfirmDialog(this, "U haven't selected any toppings. Do u want to add any toppings?", "Add Toppings", JOptionPane.YES_NO_OPTION);
    if (response == JOptionPane.YES_OPTION) {
        JOptionPane.showMessageDialog(this, "Pick any topping u would like.", "TOPPINGS", JOptionPane.INFORMATION_MESSAGE);
        return;
    }
    }
    
    price *= Bqty;
    if (price > 6.32){
       price *= 0.95;
       JOptionPane.showMessageDialog(this, "Congrats, u have received 5% discount", "DISCOUNT !!!!!!", JOptionPane.INFORMATION_MESSAGE);
    }
    tfTotalqty.setText(String.valueOf(price));
    }//GEN-LAST:event_btnTotalActionPerformed

    private void tfCashKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_tfCashKeyReleased
        double price = Double.parseDouble(tfTotalqty.getText());
        int money = Integer.parseInt(tfCash.getText());
        double change = money - price;
        if(money < price){
            JOptionPane.showMessageDialog(this,"Please pay accordingly to the price","WARNING!!!!!", JOptionPane.WARNING_MESSAGE);
            tfCash.setText("");
            tfCHANGE.setText("");
        }
        tfCHANGE.setText(String.valueOf(change));
    }//GEN-LAST:event_tfCashKeyReleased

