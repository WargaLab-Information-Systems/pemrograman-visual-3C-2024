    private void HitungActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_HitungActionPerformed
        // TODO add your handling code here:
        if (!jRadioButton1.isSelected() && !jRadioButton2.isSelected() && !jRadioButton3.isSelected() && !jRadioButton4.isSelected() && !jRadioButton5.isSelected() && !jRadioButton6.isSelected()){
            JOptionPane.showMessageDialog(this, "Isi Dulu Yaa", "Warning", JOptionPane.WARNING_MESSAGE);
        }else{
        int jumlah = Integer.parseInt(jTextField4.getText());
        int hargaItem = 0;
        int hargaTopping = 0;

        if (jRadioButton1.isSelected()){
            hargaItem = 10000;
        }else if (jRadioButton2.isSelected()){
            hargaItem = 15000;
        }else if (jRadioButton3.isSelected()){
            hargaItem = 10000;
        }else if (jRadioButton4.isSelected()){
            hargaItem = 10000;
        }else if (jRadioButton5.isSelected()){
            hargaItem = 15000;
        }else if (jRadioButton6.isSelected()){
            hargaItem = 10000;
        }

        if (jRadioButton8.isSelected()){
            hargaTopping = 5000;
        }else if (jRadioButton9.isSelected()){
            hargaTopping = 5000;
        }else if (jRadioButton7.isSelected()){
            hargaTopping = 0;
        }

        int totalHarga = (hargaItem + hargaTopping) * jumlah;

        if (totalHarga > 100000){
            double diskon = 0.05;
            totalHarga -= totalHarga * diskon;
        }
        jTextField3.setText(Integer.toString(totalHarga));

        int inihasilnyabang = Integer.parseInt(jTextField9.getText());
        int hasilsemua = inihasilnyabang - totalHarga;
        jTextField8.setText(Integer.toString(hasilsemua));
        }
    }//GEN-LAST:event_HitungActionPerformed

