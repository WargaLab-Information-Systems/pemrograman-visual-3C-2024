    private void JbklikActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JbklikActionPerformed
        // TODO add your handling code here:
        String nama = tfnama.getText();
        String gaji = tfgaji.getText();
        String jabatan = tfjabatan.getText();
        String jamkerja = tfjamkerja.getText();
        
        if(nama.isEmpty()|| jabatan.isEmpty()||gaji.isEmpty()||jamkerja.isEmpty()){
            JOptionPane.showMessageDialog(null, "Silahkan diisi");
        }else{
            int gajiint = Integer.parseInt(gaji);
            int jamkerjaint = Integer.parseInt(jamkerja);
            
            int totalgaji = gajiint*jamkerjaint;
            
            String pesan = "Nama  =" +nama+ "\n"+
                    "Jabatan  =" +jabatan+ "\n"+
                    "Total Gaji  =" +totalgaji;
            int hasil = JOptionPane.showConfirmDialog(null, "isipesan");
            if (hasil == JOptionPane.YES_OPTION){
                JOptionPane.showMessageDialog(null, pesan);
            }else{
                return;
            }
                    
        }
    }//GEN-LAST:event_JbklikActionPerformed

