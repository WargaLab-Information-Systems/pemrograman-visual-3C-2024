    private void nTotalActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_nTotalActionPerformed
        // TODO add your handling code here:
        if (!vSatu.isSelected() && !vDua.isSelected() && !vTiga.isSelected() && !vEmpat.isSelected() && !vLima.isSelected() && !vEnam.isSelected()){
            JOptionPane.showMessageDialog(this, "Diisi Dulu", "Warning", JOptionPane.WARNING_MESSAGE);
        }else{
        int jumlah = Integer.parseInt(iJumlah.getText());
        int hargaItem = 0;
        int hargaTopping = 0;

        if (vSatu.isSelected()){
            hargaItem = 15000;
        }else if (vDua.isSelected()){
            hargaItem = 15000;
        }else if (vTiga.isSelected()){
            hargaItem = 13000;
        }else if (vEmpat.isSelected()){
            hargaItem = 13000;
        }else if (vLima.isSelected()){
            hargaItem = 15000;
        }else if (vEnam.isSelected()){
            hargaItem = 15000;
        }

        if (tDua.isSelected()){
            hargaTopping = 5000;
        }else if (tTiga.isSelected()){
            hargaTopping = 3000;
        }else if (tSatu.isSelected()){
            hargaTopping = 0;
        }

        int totalHarga = (hargaItem + hargaTopping) * jumlah;

        if (totalHarga > 100000){
            double diskon = 0.05;
            totalHarga -= totalHarga * diskon;
       }
        iTotal.setText(Integer.toString(totalHarga));

        int inihasilnya = Integer.parseInt(iCash.getText());
        int hasilsemua = inihasilnya - totalHarga;
        iKembali.setText(Integer.toString(hasilsemua));
        }
    }//GEN-LAST:event_nTotalActionPerformed

