    private void txtcashKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtcashKeyReleased
        // TODO add your handling code here:
        try {
        double total = Double.parseDouble(txttotal.getText().replace("Rp. ", ""));
        int bayar = Integer.parseInt(txtcash.getText());
        if(bayar<total){
            txtkembalian.setText("Uang Kurang");
        }else if(bayar==total){
            txtkembalian.setText("Rp. 0");
        }else{
        
        double kembalian = bayar - total;
        txtkembalian.setText("Rp. "+String.valueOf(kembalian));
        }
    } catch (NumberFormatException ex) {
        txtcash.setText("");
    }
        
        
    }//GEN-LAST:event_txtcashKeyReleased

