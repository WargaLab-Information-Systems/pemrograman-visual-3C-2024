        private void hitungKembalian() {
    try {
        // Ambil nilai total harga dan cash dari TextField
        int total = Integer.parseInt(jTextFieldtotal.getText());
        int cash = Integer.parseInt(jTextFieldcash.getText());

        // Jika uang cash lebih besar atau sama dengan total, hitung kembalian
        if (cash >= total) {
            int kembalian = cash - total;
            jTextFieldkembali.setText(String.valueOf(kembalian));  // Set nilai kembalian ke TextFieldkembali
        } else {
            jTextFieldkembali.setText("Uang tidak mencukupi");
        }
    } catch (NumberFormatException e) {
        // Jika input tidak valid (misalnya, kosong atau bukan angka), kosongkan TextFieldkembali
        jTextFieldkembali.setText("");
    }
}
  
    
