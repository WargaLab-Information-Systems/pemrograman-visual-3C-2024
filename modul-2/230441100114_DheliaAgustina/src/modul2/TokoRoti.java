    private void BtnTotalActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnTotalActionPerformed
        // TODO add your handling code here:
        if (!donat.isSelected() && !panecake.isSelected() && !croissant.isSelected() && !muffin.isSelected() && !waffle.isSelected() && !mochi.isSelected()){
        JOptionPane.showMessageDialog(this, "anda belum memilih pesanan", "Peringatan", JOptionPane.WARNING_MESSAGE); 
        return;
        }
        
        
        int jumlah = Integer.parseInt(jTextFieldJumlah.getText());
        int hargaItem = 0;
        int hargaTopping = 0;
        
        if (panecake.isSelected()){
            hargaItem = 12000;
        }else if (waffle.isSelected()){
            hargaItem = 10000;
        }else if (croissant.isSelected()){
            hargaItem =7000;
        }else if (muffin.isSelected()){
            hargaItem = 4000;
        }else if (mochi.isSelected()){
            hargaItem = 6000;
        }else if (donat.isSelected()){
            hargaItem = 5000;
        }
        
        if (coklat.isSelected()){
            hargaTopping += 3000;
        }
        if (strawberry.isSelected()){
            hargaTopping += 4000;
        }
        if (notopping.isSelected()){
            hargaTopping += 0;
        }
        
        int totalHarga = (hargaItem + hargaTopping) * jumlah;
        
        if (totalHarga > 100000){
            double diskon = 0.05;
            totalHarga -= totalHarga * diskon;
            JOptionPane.showMessageDialog(this, "Selamat anda mendapatkan diskon", "Kabar gembira untuk anda", JOptionPane.WARNING_MESSAGE); 
        }
        jTextFieldtotal.setText(String.valueOf(totalHarga));
        
        
    }//GEN-LAST:event_BtnTotalActionPerformed

