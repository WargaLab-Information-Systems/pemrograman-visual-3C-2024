    private void btn_beliActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_beliActionPerformed
        int kembalian = Integer.parseInt(tf_kembali.getText());
        
        if (kembalian < 0) {
                JOptionPane.showMessageDialog(null, "uang yang kakak masukkan kurangg hiks!! tolong input ulang yaa kak><", "permisi kak....", JOptionPane.WARNING_MESSAGE);
                tf_cash.setText("");
        }else{
            String pesanann=tf_total.getText();
            String bayar=tf_cash.getText();
            String kembalii=tf_kembali.getText();
            
            int pesanan=Integer.parseInt(pesanann);
            int cash=Integer.parseInt(bayar);
            int kembali=Integer.parseInt(kembalii);
            
            String pesan= "total pesanan:"+pesanan+"\n"+
                          "bayar               :"+cash+"\n"+
                          "kembali          :"+kembali+"\n"+
                          ""+"\n"+
                          "grazie><";
            
            int confirm = JOptionPane.showOptionDialog(this,
                "kakak sudah yakin dengan pesanan kakak???",
                "halo kakkk",
                JOptionPane.YES_NO_OPTION,
                JOptionPane.QUESTION_MESSAGE, null, null, null);
                
            if(confirm == JOptionPane.YES_OPTION){
                JOptionPane.showMessageDialog(null, pesan, "Je vous remercie",JOptionPane.INFORMATION_MESSAGE);
                
                int confirmm = JOptionPane.showOptionDialog(this,
                "mau beli lagi atau tidak kak???",
                "aku mw tanya nih",
                JOptionPane.YES_NO_OPTION,
                JOptionPane.QUESTION_MESSAGE, null, null, null);
                
                if(confirmm == JOptionPane.NO_OPTION){
                    System.exit(0);
                }else{
                    tf_jumlah.setText("1");
                    tf_cash.setText("");
                    tf_kembali.setText("");
                    tf_total.setText("");
                    btngrp_menu.clearSelection();  
                    btngrp_topping.clearSelection();
                }
            }
            
        }
    }//GEN-LAST:event_btn_beliActionPerformed

