    public void hitungkembalian() {
        try {
            int totalHarga = Integer.parseInt(tf_total.getText());
            int cash = Integer.parseInt(tf_cash.getText());
            int kembalian = cash - totalHarga;

            tf_kembali.setText(String.valueOf(kembalian));
        } catch (NumberFormatException ex) {
            tf_kembali.setText("");
        }
    }
