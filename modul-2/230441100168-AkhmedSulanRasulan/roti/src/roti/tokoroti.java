    private void HitungActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_HitungActionPerformed
        // TODO add your handling code here:
        hargaRoti = 0;
        hargaTopping = 0;

        
        if (croissant.isSelected()) {
            hargaRoti = 40000;
        } else if (panada.isSelected()) {
            hargaRoti = 20000;
        } else if (rotio.isSelected()) {
            hargaRoti  = 15000;
        } else if (gambang.isSelected()) {
            hargaRoti  = 15000;
        } else if (sisir.isSelected()) {
            hargaRoti  = 5000;
        } else if (kukus.isSelected()) {
            hargaRoti = 17000;
        } else if (srikaya.isSelected()) {
            hargaRoti = 10000;
        } else if (tawar.isSelected()) {
            hargaRoti  = 10000;
        }

        
        if (hargaRoti  == 0) {
            JOptionPane.showMessageDialog(this, "Pilih satu Menu!!!", "Peringatan", JOptionPane.WARNING_MESSAGE);
            return; 
        }

        // Mengecek pilihan topping
        if (non.isSelected()) {
            hargaTopping = 0;
        } else if (coklat.isSelected()) {
            hargaTopping = 3000;
        } else if (kacang.isSelected()) {
            hargaTopping = 3000;
        } else if (keju.isSelected()) {
            hargaTopping = 3000;
        }
        
       
        
        if(tp.getSelection()==null){
            JOptionPane.showMessageDialog(null,"Pilih topping","Peringatan",JOptionPane.WARNING_MESSAGE);
            return;
        }
     
        int totalHarga = (hargaRoti + hargaTopping) * jumlah;

 
        if (totalHarga > 100000) {
            totalHarga = totalHarga - (totalHarga * 5 / 100); 
        }

        total.setText(Integer.toString(totalHarga));
        hitungakhir();
    }//GEN-LAST:event_HitungActionPerformed

