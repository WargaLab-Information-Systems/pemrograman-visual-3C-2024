private void kembaliann() {
        try{
            int melleh = Integer.parseInt(Total.getText());
            int pesse = Integer.parseInt(cash.getText());
            int sosongah =  pesse - melleh ;
            if (sosongah >=0){
                kembalian.setText(""+sosongah);
                System.out.println("\nHarga Pembelian  \t\t:"+melleh+"\nUang Diberikan \t\t\t: "+ pesse +
                        "\nKembalian   \t\t\t:"+sosongah);
            }else if(sosongah<0){
                kembalian.setText("uang anda kurang");
            }
            else{
                System.out.println("mantap");
            }
        }catch (NumberFormatException e) {
            kembalian.setText("");
        }
    }
