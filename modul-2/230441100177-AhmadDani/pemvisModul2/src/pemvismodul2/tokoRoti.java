    private void btnHitungActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHitungActionPerformed
        // TODO add your handling code here:
        int hargaRoti = 0;
        int hargaTopping = 0;
        
        if (btnGroupMenu.getSelection() == null || btnGroupTopping.getSelection() == null) {
            JOptionPane.showMessageDialog(this, "Mohon pilih satu menu dan satu topping terlebih dahulu", "Message", JOptionPane.INFORMATION_MESSAGE);
        }else {
            // Menu
            if(rbDonut.isSelected()){
                hargaRoti = 20000;
            }
            if(rbCupcake.isSelected()){
                hargaRoti = 25000;
            }
            if(rbCaramel.isSelected()){
                hargaRoti = 27000;
            }
            if(rbPanacakes.isSelected()){
                hargaRoti = 30000;
            }
            if(rbMacaron.isSelected()){
                hargaRoti = 23000;
            }
            if(rbCinnamon.isSelected()){
                hargaRoti = 23000;
            }
            
            // Topping
            if(rbNoTopping.isSelected()) {
                hargaTopping = 0;
            }
            if(rbTpNuts.isSelected()) {
                hargaTopping = 5000;
            }
            if(rbTpCoconut.isSelected()) {
                hargaTopping = 0;
            }
            
            int jumlah = Integer.parseInt(tfAmount.getText());
            
            int total = (hargaRoti + hargaTopping ) * jumlah;
            
            if(total >= 100000) {
                total *= 0.95;
            }
            
            tfTotal.setText(Integer.toString(total));
            
        }
        
       
    }//GEN-LAST:event_btnHitungActionPerformed

    private void tfCashKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_tfCashKeyReleased
        // TODO add your handling code here:
        try {
            // Ambil nilai harga dan pembayaran dari text field
            int hargaTotal = Integer.parseInt(tfTotal.getText());
            int bayar = Integer.parseInt(tfCash.getText());

            int kembalian = bayar - hargaTotal;
            
            if(bayar < hargaTotal){
                tfKembalian.setText("Uang anda kurang");
            }else {
                tfKembalian.setText(Integer.toString(kembalian));
            }
            
        } catch (NumberFormatException ex) {
            tfKembalian.setText("");
        }
    }//GEN-LAST:event_tfCashKeyReleased

