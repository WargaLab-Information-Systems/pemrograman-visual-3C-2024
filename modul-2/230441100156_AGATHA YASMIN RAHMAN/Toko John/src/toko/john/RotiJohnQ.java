    private void Total1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Total1ActionPerformed
        // TODO add your handling code here:
        if (buttonGroup1.getSelection() == null){
            JOptionPane.showMessageDialog(null, "harap memilih menu", "peringatan", JOptionPane.WARNING_MESSAGE);
            return;
        }
        if (buttonGroup2.getSelection() == null){
            JOptionPane.showMessageDialog(null, "harap memilih Isian", "peringatan", JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        int jumlahpesanan = Integer.parseInt(Angka.getText());
        if (jumlahpesanan == 0){
            JOptionPane.showMessageDialog(this,
                    "harap mengisi jumlah pesanan", "peringatan", JOptionPane.WARNING_MESSAGE);
            return;
        }
        int HargaTotal = 0;
        int diskon = 0;
        int Hargaisian = 0;
        
        if(Rbtn1.isSelected()){
            HargaTotal = 45000 * jumlahpesanan;
        }else if(Rbtn2.isSelected()){
            HargaTotal = 50000 * jumlahpesanan;
        }else if(Rbtn3.isSelected()){
            HargaTotal = 75000 * jumlahpesanan;
        }else if(Rbtn4.isSelected()){
            HargaTotal = 100000 * jumlahpesanan;
        }else if(Rbtn5.isSelected()){
           HargaTotal = 150000 * jumlahpesanan;
        }else if(Rbtn6.isSelected()){
            HargaTotal = 200000 * jumlahpesanan;
        }else {
            JOptionPane.showMessageDialog(this,"Silahkan Pilih salah satu menu","pemberitauan",JOptionPane.INFORMATION_MESSAGE);
        }
        
        if(Rto2.isSelected()){
            Hargaisian = 3000 * jumlahpesanan;
        }else if(Rto3.isSelected()){
            Hargaisian = 3000 * jumlahpesanan;
        }else if(Rto4.isSelected()){
            Hargaisian = 5000 * jumlahpesanan;
        }else if(Rto6.isSelected()){
            Hargaisian = 6000 * jumlahpesanan;
        }else if(Rto7.isSelected()){
           Hargaisian = 3000 * jumlahpesanan;
        }else if(Rto8.isSelected()){
            Hargaisian = 7000 * jumlahpesanan;
        }else {
            JOptionPane.showMessageDialog(this,"isi Isian yang anda suka","pemberitauan",JOptionPane.INFORMATION_MESSAGE);
        }
        
    HargaTotal += Hargaisian;
//    HargaTotal= HargaTotal +  Hargaisian;
    if (HargaTotal > 100000){
        diskon = HargaTotal * 5 / 100;
        HargaTotal -= diskon;
    }
    hasilTotal.setText(String.valueOf(HargaTotal));
    }//GEN-LAST:event_Total1ActionPerformed

