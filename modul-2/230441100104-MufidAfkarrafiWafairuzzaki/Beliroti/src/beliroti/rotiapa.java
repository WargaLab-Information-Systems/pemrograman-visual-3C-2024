    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
if(roti.getSelection() == null){
    JOptionPane.showMessageDialog(null, "Tolong pilih menu roti");
    return;
}

if(teksJumlah.getText().isEmpty()){
    JOptionPane.showMessageDialog(null, "Jumlah harus diisi");
    return;
}else if(Integer.parseInt(teksJumlah.getText()) == 0){
    JOptionPane.showMessageDialog(null, "Ay yo Bro isi yang benar");
    return;
}

int totalHarga = 0;
int hargaRoti = 0;
int hargaTopping = 0;
int jumlah = Integer.parseInt(teksJumlah.getText());

// Cek setiap JCheckBox topping yang dipilih dan tambahkan harga topping
if(tporeo.isSelected()) {
    hargaTopping += 2000;
}
if(tpkacang.isSelected()){
    hargaTopping += 1000;
}
if(tpkeju.isSelected()) {
    hargaTopping += 3000;
}
if(tpmeses.isSelected()) {
    hargaTopping += 4000;
}
if(tpnanas.isSelected()) {
    hargaTopping += 2000;
}
if(tpalpukat.isSelected()) {
    hargaTopping += 4000;
}

if(rttawar.isSelected()) {
    hargaRoti = 5000;
}else if(rto.isSelected()) {
    hargaRoti = 1000;
}else if(rtabon.isSelected()) {
    hargaRoti = 15000;
}else if(rtpizza.isSelected()) {
    hargaRoti = 17000;
}else if(rtbangor.isSelected()) {
    hargaRoti = 7000;
}else if(rtngawi.isSelected()) {
    hargaRoti = 8000;
}

totalHarga = (hargaRoti + hargaTopping) * jumlah;

if(totalHarga >= 100000){
    double diskon = 0.05 * totalHarga;
    totalHarga -= diskon;
}

teksTotal.setText(String.valueOf(totalHarga));

    }//GEN-LAST:event_jButton3ActionPerformed

