public void hitungkembalian() {
    try {
        int totalHarga = Integer.parseInt(teksTotal.getText());
        int cash = Integer.parseInt(teksCash.getText());
        String kembalian;

        if (cash >= totalHarga) {
            kembalian = String.valueOf(cash - totalHarga); 
        } else {
            kembalian = "Uang Kurang"; 
        }

        teksKembalian.setText(kembalian); 
    } catch (NumberFormatException ex) {
        teksKembalian.setText(""); 
    }
}


