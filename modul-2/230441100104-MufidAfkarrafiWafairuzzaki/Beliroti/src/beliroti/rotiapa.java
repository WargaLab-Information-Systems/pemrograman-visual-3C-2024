    private void teksCashActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_teksCashActionPerformed
    int total = Integer.parseInt(teksTotal.getText());
    int cash = Integer.parseInt(teksCash.getText());
    String kembalian; 
    
    if (total > 0 && cash >= total) {
        kembalian = String.valueOf(cash - total);
    } else if (cash < total) {
        kembalian = "Uang Kurang";  
//        JOptionPane.showMessageDialog(null, "Uang Kurang", "Peringatan", JOptionPane.INFORMATION_MESSAGE);
    } else {
        kembalian = "0";
    }

    teksKembalian.setText(kembalian); 

    }//GEN-LAST:event_teksCashActionPerformed

