    private void KirimButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_KirimButtonActionPerformed
        if (jTextField1.getText().isEmpty() || 
        jTextField2.getText().isEmpty() || 
        jTextField3.getText().isEmpty() || 
        jTextField4.getText().isEmpty() || 
        jTextField5.getText().isEmpty() || 
        jTextField6.getText().isEmpty() || 
        jTextField7.getText().isEmpty() ||
        (!jRadioButton1.isSelected() && !jRadioButton2.isSelected()) || 
        jComboBox1.getSelectedIndex() == -1 || 
        (!jCheckBox1.isSelected() && !jCheckBox2.isSelected() && !jCheckBox3.isSelected() && !jCheckBox4.isSelected() && !jCheckBox5.isSelected())) {
        
        
        JOptionPane.showMessageDialog(this, "Data harus diisi semua", "Peringatan", JOptionPane.WARNING_MESSAGE);
        return;
        }
    
        frame2 frm2 = new frame2();
        frm2.jLabel11.setText(jTextField1.getText());
        frm2.jLabel12.setText(jTextField2.getText());
        frm2.jLabel13.setText(jTextField3.getText());
        frm2.jLabel14.setText(jTextField4.getText());
        frm2.jLabel15.setText(jTextField5.getText());
        frm2.jLabel16.setText(jTextField6.getText());
        frm2.jLabel17.setText(jTextField7.getText());
        
        String genderGroup = "";  
        if (jRadioButton1.isSelected()) {
            genderGroup = "Perempuan";
        } else if (jRadioButton2.isSelected()) {
            genderGroup = "Laki-Laki";
        }
        frm2.jLabel19.setText(genderGroup);
        
        StringBuffer mapel = new StringBuffer();
    if (jCheckBox1.isSelected()) {
        mapel.append(jCheckBox1.getText()).append(", ");
    }
    if (jCheckBox2.isSelected()) {
        mapel.append(jCheckBox2.getText()).append(", ");
    }
    if (jCheckBox3.isSelected()) {
        mapel.append(jCheckBox3.getText()).append(", ");
    }
    if (jCheckBox4.isSelected()) {
        mapel.append(jCheckBox4.getText()).append(", ");
    }

    if (mapel.length() > 0) {
        mapel.setLength(mapel.length() - 2);
    }

    frm2.jLabel18.setText(mapel.toString());

    String kelasDipilih = jComboBox1.getSelectedItem().toString();
    frm2.jLabel20.setText(kelasDipilih);
    

    frm2.setVisible(true);
    }//GEN-LAST:event_KirimButtonActionPerformed

