    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
//        butten 
        jFrame2 frame2 =new jFrame2();
        frame2.txtnama.setText(txtnama.getText());
        frame2.txtTTL.setText(txtTTL.getText());
        frame2.txtNISN.setText(txtNISN.getText());
        frame2.txtkls.setText(txtkls.getText());
        frame2.txtasal.setText(txtasal.getText());
        if(rbcowo.isSelected()){
            frame2.txtJK.setText("laki-laki");
        }else if(rbcewe.isSelected()){
            frame2.txtJK.setText("Perempuan");
        }
        
        String bidang =(String)cbxbidang.getSelectedItem().toString();
        frame2.txtbidang.setText(bidang);
        if(ckbdata.isSelected()){   
            if(txtnama.getText().isEmpty() 
                    || txtTTL.getText().isEmpty()
                    || txtNISN.getText(). isEmpty() 
                    ||txtkls.getText(). isEmpty() 
                    || txtasal.getText(). isEmpty()
                    )JOptionPane.showMessageDialog(null, "Data belum lengkap");
            else{
                     frame2.setVisible(true);
                     dispose();
                }
        }else if(ckbdata.isSelected()){{
            JOptionPane.showMessageDialog(null, "Data belum lengkap");  
            }    
        }
    }//GEN-LAST:event_jButton1ActionPerformed

