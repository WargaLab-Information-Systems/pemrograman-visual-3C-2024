    private void jbHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbHapusActionPerformed
        // TODO add your handling code here:
        txtNama.setText(null);
        txtTanggal.setText(null);
        txtTempat.setText(null);
        txtNoTelp.setText(null);
        buttonGroup1.clearSelection();
        txtEmail.setText(null);
        txtAlamat.setText(null);
        txtProdi.setText(null);
        // Set JComboBox kembali ke default tanpa menambahkan "pilih treatment"
        cbFakultas.insertItemAt("Pilih Treatment", 0); // Membuat tidak ada yang terpilih
        cbFakultas.setSelectedIndex(0); // Membuat tidak ada yang terpilih
        
        // Hapus item "pilih treatment" dari JComboBox jika ada
        if (cbFakultas.getItemAt(0).equals("pilih treatment")) {
            cbFakultas.removeItemAt(0);
        }
    }//GEN-LAST:event_jbHapusActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
                                       
    // TODO add your handling code here:
    FrameOutput FI = new FrameOutput();

    // Cek apakah ada kolom yang belum diisi
    if (txtNama.getText().isEmpty() || txtTanggal.getText().isEmpty() || txtTempat.getText().isEmpty() ||
        txtNoTelp.getText().isEmpty() || txtEmail.getText().isEmpty() || txtAlamat.getText().isEmpty() ||
        txtProdi.getText().isEmpty() || cbFakultas.getSelectedItem() == null || 
        (!rbLaki.isSelected() && !rbPerempuan.isSelected())) {
        
        txtPeringatan.setText("Mohon isi semua kolom dan pilih jenis kelamin.");
    } else if (!cbData.isSelected()) {
        txtPeringatan.setText("Mohon dicentang.");
    } else {
        // Jika semua kolom sudah terisi, data disimpan
        FI.txtNama1.setText(txtNama.getText());
        FI.txtTanggal1.setText(txtTanggal.getText());
        FI.txtTempat1.setText(txtTempat.getText());
        if (rbLaki.isSelected()) {
            FI.txtKelamin1.setText("Laki Laki");
        } else {
            FI.txtKelamin1.setText("Perempuan");
        }
        FI.txtNoTelp1.setText(txtNoTelp.getText());
        FI.txtEmail1.setText(txtEmail.getText());
        FI.txtAlamat1.setText(txtAlamat.getText());
        FI.txtProdi1.setText(txtProdi.getText());
        String fakultas = cbFakultas.getSelectedItem().toString();
        FI.txtFakultas1.setText(fakultas);
        FI.setVisible(true);
        this.dispose();
    }
    }//GEN-LAST:event_jButton2ActionPerformed

