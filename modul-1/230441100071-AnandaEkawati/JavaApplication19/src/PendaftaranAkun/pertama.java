    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // tombol simpan:
        kedua n = new kedua ();
        n.txtNama.setText(txtNama.getText());
        n.txtTTL.setText(txtTTL.getText());
        n.txtAlamat.setText(txtAlamat.getText());
        n.txtTelp.setText(txtTelepon.getText());
        n.txtAnak.setText(txtAnak.getText());
        n.txtZodiak.setText(txtZodiak.getText());
        n.txtGolDrh.setText(txtGol.getText());
        if (rbPr.isSelected()){
            n.txtJenisKelamin.setText("perempuan");
        }
        if (rbLk.isSelected()){
            n.txtJenisKelamin.setText("Laki-laki");
        }
        String o =(String)cbxAgama.getSelectedItem();
        n.txtAgama.setText(o);
        if (cbxDataInput.isSelected()){
            if(txtNama.getText().isEmpty()
                    || txtTTL.getText().isEmpty()
                    || txtAlamat.getText().isEmpty()
                    || txtTelepon.getText().isEmpty()
                    || txtAnak.getText().isEmpty()
                    || txtZodiak.getText().isEmpty()
                    || txtGol.getText().isEmpty()
                    )
            {JOptionPane.showMessageDialog(null, "Data tidak lengkap");
            }
            else{
                    n.setVisible(true);
                    dispose();
            }
            }else {
            {JOptionPane.showMessageDialog(null, "Data tidak lengkap");
        }
    }
    }//GEN-LAST:event_jButton3ActionPerformed

