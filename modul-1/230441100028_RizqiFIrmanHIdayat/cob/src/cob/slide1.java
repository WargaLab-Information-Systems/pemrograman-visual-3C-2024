    private void SaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SaveActionPerformed
if (Nama.getText().isEmpty() || 
        TempatL.getText().isEmpty() || 
        TanggalL.getText().isEmpty() || 
        Alamat.getText().isEmpty() || 
        Notlp.getText().isEmpty() || 
        Email.getText().isEmpty() ) {
        
        JOptionPane.showMessageDialog(this, 
            "Harap isi semua biodata dengan benar!", 
            "Peringatan", 
            JOptionPane.WARNING_MESSAGE);
        return;
    }  
    if (!JenisK1.isSelected() && !JenisK2.isSelected()) {
        JOptionPane.showMessageDialog(this, 
            "Harap isi semua biodata!", 
            "Peringatan", 
            JOptionPane.WARNING_MESSAGE);
        return;
    }
        if (!Apakah.isSelected()) {
        JOptionPane.showMessageDialog(this, 
            "Anda harus mencentang 'Data yang sudah saya masukkan benar' sebelum menyimpan!", 
            "Peringatan", 
            JOptionPane.WARNING_MESSAGE);
    } else {
        slide2 n = new slide2();
        n.Nama1.setText(Nama.getText());
        n.Tempat1.setText(TempatL.getText());
        n.Tanggal1.setText(TanggalL.getText());
                if (JenisK1.isSelected()){
            n.JenisK5.setText("Laki - laki");
        }
        else if (JenisK2.isSelected()){
            n.JenisK5.setText("Perempuan");
        }
        n.Alamat1.setText(Alamat.getText());
        n.Notlp1.setText(Notlp.getText());
        n.Email1.setText(Email.getText());
        n.Divisi1.setText(Divisi.getText());
                int selectedIndex = Pekerjaan.getSelectedIndex();
        if (selectedIndex == 0){
            n.Pekerjaan1.setText("");
        }
        else {
            String value;
            value = Pekerjaan.getSelectedItem().toString();
            n.Pekerjaan1.setText(value);
        }
                n.setVisible(true);
        this.setVisible(false);
        }     
    }//GEN-LAST:event_SaveActionPerformed

