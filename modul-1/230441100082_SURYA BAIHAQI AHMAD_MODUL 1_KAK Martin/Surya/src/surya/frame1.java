    private void btnsimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsimpanActionPerformed
        // TODO add your handling code here:
        if(!data.isSelected()){
            JOptionPane.showMessageDialog(this, "Anda harus memastikan bahwa data anda sudah benar mengisi data", "Peringatan", JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        String Nama = nama.getText();
        String Alamat = alamat.getText();
        String Email = email.getText();
        String Namawisata = (String) jComboBox1.getSelectedItem();
        String Jeniskelamin = "";
        if(laki.isSelected()){
            Jeniskelamin ="laki-laki";
        }
        if(perempuan.isSelected()){
            Jeniskelamin ="perempuan";
        }
        
        String Jeniswisata ="";
        if(Agrowisata.isSelected()){
            Jeniswisata = "Agrowisata";
        }
        if(Industri.isSelected()){
            Jeniswisata = "Industri";
        }
        if(Alam.isSelected()){
            Jeniswisata = "alam";
        }
        
        frame2 p = new frame2(Nama, Alamat, Email, Namawisata, Jeniskelamin, Jeniswisata);
        p.setVisible(true);
        p.setLocationRelativeTo(null);
        this.dispose();
    }//GEN-LAST:event_btnsimpanActionPerformed

