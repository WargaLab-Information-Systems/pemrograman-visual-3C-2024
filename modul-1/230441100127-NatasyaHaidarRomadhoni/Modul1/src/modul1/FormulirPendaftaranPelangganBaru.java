    private void btnResetActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnResetActionPerformed
    // ActionListener untuk Button Keluar
    btnReset.addActionListener(new java.awt.event.ActionListener() {
    public void actionPerformed(java.awt.event.ActionEvent evt) {
        // Mengosongkan semua field dalam formulir
        tfNamaPelanggan.setText("");
        tfAlamatPelanggan.setText("");
        tfNoTelepon.setText("");
        
        // Mengosongkan pilihan RadioButton di ButtonGroup
        btgrjeniskelamin.clearSelection(); // Pastikan kamu menggunakan ButtonGroup

        // Mengembalikan ComboBox dan DateChooser ke default
        cbJenisTreatment.setSelectedIndex(0);
        dcTanggalTreatmentDiinginkan.setDate(null);
        
        // Mengosongkan checkbox syarat dan ketentuan
        cbSetujuSyaratKetentuan.setSelected(false);
    }
});

    }//GEN-LAST:event_btnResetActionPerformed

    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
    // Mengecek apakah checkbox "Setuju Syarat dan Ketentuan" sudah dicentang
    if (!cbSetujuSyaratKetentuan.isSelected()) {
        // Tampilkan pesan peringatan jika checkbox belum dicentang
        JOptionPane.showMessageDialog(null, "Anda harus menyetujui syarat dan ketentuan sebelum menyimpan!", "Peringatan", JOptionPane.WARNING_MESSAGE);
    } else {
        // Ambil data dari field di frame pertama
        String nama = tfNamaPelanggan.getText();
        String alamat = tfAlamatPelanggan.getText();
        String telepon = tfNoTelepon.getText();
        String jenisKelamin = rbLakiLaki.isSelected() ? "Laki-Laki" : "Perempuan";
        String treatment = cbJenisTreatment.getSelectedItem().toString();
        String tanggal1 = dcTanggalTreatmentDiinginkan.getDate().toString();

        // Ambil status checkbox (Setuju/Tidak Setuju)
        String SetujuSyaratKetentuan = cbSetujuSyaratKetentuan.isSelected() ? "Setuju" : "Tidak Setuju";

        // Buat objek frame kedua dan kirim data ke dalamnya
        FormulirKeDua formKeDua = new FormulirKeDua(nama, alamat, telepon, jenisKelamin, treatment, tanggal1, SetujuSyaratKetentuan);
        formKeDua.setVisible(true);

        // Menyembunyikan frame pertama (opsional)
        dispose();
    }
    }//GEN-LAST:event_btnSimpanActionPerformed

