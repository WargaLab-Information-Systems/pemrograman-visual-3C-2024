    private void simpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_simpanActionPerformed
        // TODO add your handling code here:
        if (!checkboxdata.isSelected()) {
            JOptionPane.showMessageDialog(this, "Ups!!! Pastikan Data Yang Anda Masukkan Sudah Benar, Lalu Centang!!!", "Peringatan", JOptionPane.WARNING_MESSAGE);
        }else if (!lakilaki.isSelected() && !perempuan.isSelected()) {
        JOptionPane.showMessageDialog(this, "Ups!!! Pastikan Anda Sudah Memilih Jenis Kelamin!!!", "Peringatan", JOptionPane.WARNING_MESSAGE);
        }else{
            String nama1 = nama.getText();
            String prodi1 = prodi.getSelectedItem().toString();
            String jeniskelamin = lakilaki.isSelected()? "Laki-Laki" : "Perempuan";
            String tempat1 = tempat.getText();
            String tanggal1 = tanggal.getSelectedItem().toString();
            String bulan1 = bulan.getSelectedItem().toString();
            String tahun1 = tahun.getSelectedItem().toString();
            String alamat1 = alamat.getText();
            String telepon1 = telepon.getText();
            String email1 = email.getText();
            String hobi1 = hobi.getText();
            String ukm1 = ukmbaru.getSelectedItem().toString();
            String alasan1 = alasan.getText();
            
        // Untuk Menampilkan Ke Output Hasil
        HasilFormulirUKMFT hasil = new HasilFormulirUKMFT(nama1, prodi1, jeniskelamin, tempat1, tanggal1, bulan1, tahun1, alamat1, telepon1, email1, hobi1, ukm1, alasan1);
        hasil.setVisible(true);
        dispose();
        }
    }//GEN-LAST:event_simpanActionPerformed
        
        
