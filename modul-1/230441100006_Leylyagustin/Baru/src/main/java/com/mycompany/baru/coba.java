    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        if(ceklis.isSelected()){
        nama = iNama.getText();
        lahir = iTTL.getText(); 
        kaka = iKK.getText();
        if (iL.isSelected()){
            kelamin = "Laki-laki";
        } else if (iP.isSelected()){
            kelamin = "Perempuan";
        } else {
            kelamin = "";
        }
        alamat = iAlamat.getText();
        notelp = iTelp.getText();
        status = iStatus.getText();
        
        jaminan = iJaminan.getSelectedItem().toString();
        
        if (!nama.isEmpty() && !lahir.isEmpty() && !kaka.isEmpty() && !kelamin.isEmpty() && !alamat.isEmpty() && !notelp.isEmpty() && !status.isEmpty() && !jaminan.isEmpty()){
           coba2 saya = new coba2();
           saya.jTextField1.setText(nama);
           saya.jTextField2.setText(lahir);
           saya.jTextField3.setText(kaka);
           saya.jTextField4.setText(kelamin);
           saya.jTextField5.setText(alamat);
           saya.jTextField6.setText(notelp);
           saya.jTextField7.setText(status);
           saya.jTextField8.setText(jaminan);
           saya.setVisible(true);
           dispose();
        } else{
            error1.setText("Isi dulu!!");
        }
        }else{
           error.setText("Ceklis dulu!!!");
        }
        
    }//GEN-LAST:event_jButton3ActionPerformed

