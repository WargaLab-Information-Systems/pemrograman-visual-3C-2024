    private void simpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_simpanActionPerformed
        // TODO add your handling code here:
        fpendaftaran i = new fpendaftaran();
        i.exnama.setText(namaawal.getText());
        i.exttd.setText(ttl.getText());
        if (cowok.isSelected()){
            i.exjeniskelamain.setText("mele");
        }
         if (cewek.isSelected()){
            i.exjeniskelamain.setText("femele");
        }
         if (dll.isSelected()){
            i.exjeniskelamain.setText("no binery");
        }
        i.exemail.setText(email.getText());
        i.exnotlp.setText(noHP.getText());
        i.exkota.setText(kota.getText());
        i.exwilayah.setText(provinsi.getText());
        i.exnegara.setText(negara.getText());
        i.exalasan .setText(alasan.getText());
        i.exlokasi.setText (lokasi.getSelectedItem().toString());
        
        if (!jCheckBox1.isSelected()) {
          if(namaawal.getText().isEmpty() 
                || ttl.getText().isEmpty()
                || email.getText(). isEmpty() 
                ||noHP.getText(). isEmpty() 
                ||kota.getText(). isEmpty()
                ||provinsi.getText(). isEmpty()         
                ||negara.getText(). isEmpty()
                ||alasan.getText(). isEmpty()
                ) JOptionPane.showMessageDialog(null, "verify that you are over 21 years of age and agree to the terms and conditions.", "WARNING", JOptionPane.WARNING_MESSAGE);
        }else{
            if(namaawal.getText().isEmpty() 
                || ttl.getText().isEmpty()
                || email.getText(). isEmpty() 
                ||noHP.getText(). isEmpty() 
                ||kota.getText(). isEmpty()
                ||provinsi.getText(). isEmpty()         
                ||negara.getText(). isEmpty()
                ||alasan.getText(). isEmpty()
                ){
                JOptionPane.showMessageDialog(null, "data gak lengkap.", "WARNING", JOptionPane.WARNING_MESSAGE);
            }else{
                 i.setVisible(true);
                         dispose();
            }
           
        }
       
    }//GEN-LAST:event_simpanActionPerformed

