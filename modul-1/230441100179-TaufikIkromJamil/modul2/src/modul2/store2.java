    private void penghitungan() {
        try {
            double hargasetelahdiskon = Double.parseDouble(total.getText());
            double uangmasuk = Double.parseDouble(bayar.getText());
            double kembaliann = uangmasuk - hargasetelahdiskon;

            if (kembaliann >= 0) {
                kembalian.setText(String.valueOf(kembaliann));
            } else {
                kembalian.setText("Uang Tidak Cukup");
            }
        } catch (NumberFormatException e) {
            kembalian.setText("Input Tidak Sesuai");
        }
    }
