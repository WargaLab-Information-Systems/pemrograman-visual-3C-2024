    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
    if (Nama.getText().isEmpty() || 
        TempatLahir.getText().isEmpty() || 
        TanggalLahir.getText().isEmpty() || 
        Alamat.getText().isEmpty() || 
        NoTelp.getText().isEmpty() || 
        Email.getText().isEmpty() || 
        Prodi.getText().isEmpty()) {
        
        JOptionPane.showMessageDialog(this, 
            "Harap isi semua biodata!", 
            "Peringatan", 
            JOptionPane.WARNING_MESSAGE);
        return;
    }
    if (!JenisK1.isSelected() && !JenisK2.isSelected()) {
        JOptionPane.showMessageDialog(this, 
            "Harap isi semua biodata!", 
            "Peringatan", 
            JOptionPane.WARNING_MESSAGE);
        return;
    }
    if (!Data.isSelected()) {
        JOptionPane.showMessageDialog(this, 
            "Anda harus mencentang kotak 'Data yang sudah saya masukkan benar' sebelum menyimpan!", 
            "Peringatan", 
            JOptionPane.WARNING_MESSAGE);
    } else {
        FormulirHasil n = new FormulirHasil();
        n.Hasil1.setText(Nama.getText());
        n.Hasil2.setText(TempatLahir.getText());
        n.Hasil3.setText(TanggalLahir.getText());
            if (JenisK1.isSelected()){
            n.Hasil4.setText("Laki - laki");
        }
        else if (JenisK2.isSelected()){
            n.Hasil4.setText("Perempuan");
        }
        n.Hasil5.setText(Alamat.getText());
        n.Hasil6.setText(NoTelp.getText());
        n.Hasil7.setText(Email.getText());
        n.Hasil8.setText(Prodi.getText());
        int selectedIndex = Fakultas.getSelectedIndex();
        if (selectedIndex == 0){
            n.Hasil9.setText("");
        }
        else {
            String value;
            value = Fakultas.getSelectedItem().toString();
            n.Hasil9.setText(value);
        }
            n.setVisible(true);
        this.setVisible(false);
        } 
 
        

    }//GEN-LAST:event_jButton3ActionPerformed

