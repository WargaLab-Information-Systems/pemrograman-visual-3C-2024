    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        // TODO add your handling code here:
        if(!checkBox.isSelected()){
            JOptionPane.showMessageDialog(null,"Anda harus mencentang checkbox sebelum menyimpan data!","Peringatan",JOptionPane.WARNING_MESSAGE);
        }else{
            nama = txtNama.getText();
            nomor =txtNomor.getText();
            if(radioL.isSelected()){
                jenisKelamin = "Laki-Laki";
            }else if(radioP.isSelected()){
                jenisKelamin = "perempuan";
            }
            alamat = txtAreaAlamat.getText();
            email = txtEmail.getText();
            course = comboBoxCourse.getSelectedItem().toString();
        }if (nama.isEmpty()||jenisKelamin.isEmpty()||alamat.isEmpty()||email.isEmpty()||course == null||nomor.isEmpty()){
           JOptionPane.showMessageDialog(null,"Data anda belum lengkap! Silakan isi semua field.", "Peringatan", JOptionPane.WARNING_MESSAGE);
        }else{
            Frame2 frame = new Frame2(nama,nomor,jenisKelamin,alamat,email,course);
        
            frame.setVisible(true);
            dispose();
        }
        
    }//GEN-LAST:event_btnSimpanActionPerformed

