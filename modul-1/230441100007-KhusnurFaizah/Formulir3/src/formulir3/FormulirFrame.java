private void BtnSimpanActionPerformed(java.awt.event.ActionEvent evt) {                                          
        try {
            String nama = JawabNama.getText();
            String alamat = JawabAlmt.getText();
            String jeniskelamin;
                if (Laki.isSelected()){
                    jeniskelamin = "Laki-Laki";
                }else if (Per.isSelected()){
                    jeniskelamin = "Perempuan";
                }
                else{
                    jeniskelamin = "Rahasia";
                }
            String Agama = JwbAgama.getSelectedItem().toString();
            String Hobi = JawabHobi.getText();
            String Kwn = JawabKwn.getText();
            String tempat = JawabTTL.getText();
            String Notelp = JawabNO.getText();
            
            Formulir4 databerhasil = new Formulir4(nama, alamat, jeniskelamin, Agama ,Hobi, Kwn, tempat, Notelp);
            databerhasil.setVisible(true);
            
            reset();
        } catch (Exception e) { //KESALAHAN TRY
            e.printStackTrace(); //apa yg slh selama eksekusi
        }
        BtnSimpan.addActionListener(new java.awt.event.ActionListener() {
            public void actionPerformed(java.awt.event.ActionEvent evt) {
                if (!DataBenar.isSelected()) {
                    JOptionPane.showMessageDialog(null, "Apakah Data yang Anda Masukkan Sudah Benar? Jika Sudah Klik Tombol Centang", "Peringatan", JOptionPane.WARNING_MESSAGE);
                }else {
                    Formulir4 databerhasil = new Formulir4();
                    databerhasil.setVisible(true);
                }}
            });
    }                  

