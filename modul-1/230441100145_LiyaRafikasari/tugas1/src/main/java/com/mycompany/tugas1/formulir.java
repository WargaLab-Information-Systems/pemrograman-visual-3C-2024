    private void HasilActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_HasilActionPerformed
     if(cbSyarat.isSelected()){
            aNama = jTextnama2.getText();
            aTTL = jTexttempat_lahir2.getText();
            aTanggalL = jTexttanggal_lahir2.getText();
            aalamat = jTextalamat2.getText();
            aNoTelp = jTextno_telp2.getText();
            aEmil = jTextemail2.getText();
            aProdi = jTextprodi2.getText();
            Fakultas = (String)jTextFieldFakultas.getSelectedItem().toString();
            if (laki_laki2.isSelected()){
                kelamin = "Laki-Laki"; 
            } else if (perempuan2.isSelected()){
                kelamin = "Perempuan";
            } else {
                kelamin = "";
            }
            if (!aNama.isEmpty() && !aTTL.isEmpty() && !aTanggalL.isEmpty() && !aalamat.isEmpty() && !aNoTelp.isEmpty() && !aEmil.isEmpty() && !aProdi.isEmpty() && !Fakultas.isEmpty() && !kelamin.isEmpty()){
                formulir2 aku = new formulir2();
                aku.nama1.setText(aNama);
                aku.tempat_lahir1.setText(aTTL);
                aku.tanggal_lahir1.setText(aTanggalL);
                aku.jenis_kelamin1.setText(aalamat);
                aku.alamat1.setText(aNoTelp);
                aku.no_telp1.setText(aEmil);
                aku.email1.setText(aEmil);
                aku.prodi1.setText(Fakultas);
                aku.fakultas1.setText(kelamin);
                aku.setVisible(true);    
                dispose();
            } else{
                jLabel5.setText("Isi dulu");
                }
}else {
//            lblSyarat.setText("Mohon centang terlebih dahulu");
            JOptionPane.showMessageDialog(null,"Harap Checklist Terlebih Dahulu!", "Peringatan", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_HasilActionPerformed


