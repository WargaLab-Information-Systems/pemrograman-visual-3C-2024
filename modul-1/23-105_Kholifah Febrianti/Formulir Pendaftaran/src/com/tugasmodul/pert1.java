    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        pert2 frame1 = new pert2();
        frame1.nama.setText(nama.getText());
        frame1.tgl.setText(tgl.getText());
        if (rbLk.isSelected()){
            frame1.jk.setText("Laki-Laki");
        }
        if (rbPr.isSelected()){
            frame1.jk.setText("Perempuan");
        }
        frame1.jenjang.setText(jenjang.getSelectedItem().toString());
        frame1.nomor.setText(nomor.getText());
        frame1.program.setText(program.getSelectedItem().toString());
        if (papa.isSelected()){
            frame1.hari.setText("Senin&Kamis");
        }
        if (pipi.isSelected()){
            frame1.hari.setText("Selasa&Jum'at");
        }
        if (popo.isSelected()){
            frame1.hari.setText("Rabu&Sabtu");
        }
        if (jam1.isSelected()){
            frame1.waktu.setText("14.00-15.30");
        }
         if (jam2.isSelected()){
            frame1.waktu.setText("16.00-17.30");
        }
          if (jam3.isSelected()){
            frame1.waktu.setText("19.00-20.30");
        }
          if (konfir.isSelected()){
              frame1.setVisible(true);
                dispose();
          }else{
              JOptionPane.showMessageDialog(null, "PASTIKAN ANDA SUDAH MENGISI SEMUA");
          }
        
    }//GEN-LAST:event_jButton1ActionPerformed

