    private void simpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_simpanActionPerformed
        // TODO add your handling code here:
        
        if (nama.getText().isEmpty()||
            tempat.getText().isEmpty()||
            tanggal.getText().isEmpty()||
            alamat.getText().isEmpty()||
            telepon.getText().isEmpty()||
            email.getText().isEmpty()||
            prodi.getText().isEmpty())  {
            
            JOptionPane.showMessageDialog(this,
                    "Lengkapi Semua Data");
        return;
        
        }
        if(jblaki.isSelected()&& jbperempuan.isSelected()) {
        JOptionPane.showMessageDialog(this, 
                "Lengkapi terlebih dahulu", "Warning",
                JOptionPane.WARNING_MESSAGE);
        return;
        }
        
        if (!konfir.isSelected()) {
        // Jika CheckBox tidak dicentang, tampilkan peringatan
        JOptionPane.showMessageDialog(this, 
            "Ceklist Terlebih Dahulu", 
            "Warning", 
            JOptionPane.WARNING_MESSAGE);
    } else {
        // Jika CheckBox dicentang, lanjutkan ke form berikutnya
        tp1formulir tp1 = new tp1formulir();
        tp1.nama2.setText(nama.getText());
        tp1.tempat2.setText(tempat.getText());
        tp1.tanggal2.setText(tanggal.getText());
            if (jblaki.isSelected()){
                tp1.jbjenis.setText("Laki-Laki");
            }
            else if (jbperempuan.isSelected()){
                tp1.jbjenis.setText("Perempuan");
            }
        tp1.alamat2.setText(alamat.getText());    
        tp1.telepon2.setText(telepon.getText());
        tp1.email2.setText(email.getText());
        tp1.prodi2.setText(prodi.getText());
        
        int selectedIndex = fakultas.getSelectedIndex();
        
        if (selectedIndex==0){
            tp1.fakultas2.setText("");
        }
        else {
        String fk;
        fk = fakultas.getSelectedItem().toString();
        tp1.fakultas2.setText(fk);
        }
        
        
        tp1.setVisible(true);
        this.dispose();  // Ini akan menutup form penuh

        }
    }//GEN-LAST:event_simpanActionPerformed

