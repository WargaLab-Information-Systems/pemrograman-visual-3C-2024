    private void sendActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_sendActionPerformed
        
        if (!chbsetuju.isSelected()) {
            JOptionPane.showMessageDialog(this, "Anda harus mencentang kotak persetujuan terlebih dahulu!", "Peringatan", JOptionPane.WARNING_MESSAGE);
        } else {
            Pertanyaan1Halaman2 Hal2 = new Pertanyaan1Halaman2();
            
            Hal2.txtnama2.setText(txtnama.getText());
            Hal2.txtnim2.setText(txtnim.getText());
            
            if(rdblaki.isSelected()== true){
                Hal2.txtjeniskelamin2.setText(rdblaki.getText());
            }else if(rdbpr.isSelected()== true){
                Hal2.txtjeniskelamin2.setText(rdbpr.getText());
            }else {
               Hal2.txtjeniskelamin2.setText("");
            }
            
            
            Hal2.txtangkatan2.setText(txtangkatan.getText());
            Hal2.txtemail2.setText(txtemail.getText());
            
            String jurusan = cmbjurusan.getSelectedItem().toString();
            Hal2.txtjurusan2.setText(jurusan);
            
            Hal2.txttelepon2.setText(txttelepon.getText());
            Hal2.txtkota2.setText(txtkota.getText());
            Hal2.txaalasanhal2.setText(txaalasan.getText());
            
            if(txtnama.getText().trim().isEmpty()||txtnim.getText().trim().isEmpty()||
                   (!rdblaki.isSelected() && !rdbpr.isSelected())||cmbjurusan.getSelectedIndex()==0||txtangkatan.getText().trim().isEmpty()||txtemail.getText().trim().isEmpty()||txttelepon.getText().trim().isEmpty()||
                txtkota.getText().trim().isEmpty()||txaalasan.getText().trim().isEmpty()){JOptionPane.showMessageDialog(this,"ISI DATA DENGAN LENGKAP", "Peringatan", JOptionPane.INFORMATION_MESSAGE);
            }else {
                Hal2.setVisible(true);
            }
          
        }
        
       
                
    }//GEN-LAST:event_sendActionPerformed

