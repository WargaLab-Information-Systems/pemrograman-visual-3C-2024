    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        
       if (validasi.isSelected()) {
        String nama = teksNama.getText();
        String TTL = teksTTL.getText();
        String alamat = teksAlamat.getText();
        String nomor = teksNoHP.getText();
        String email = teksEmail.getText();
        String pendidikan = cmbPendidikan.getSelectedItem().toString();
    
        String gender = "";
        if (btnLakiLaki.isSelected()) {
            gender = "Laki-laki";
        } else if (btnPerempuan.isSelected()) {
            gender = "Perempuan";
        }
        
        if (nama.isEmpty()){
        JOptionPane.showMessageDialog(null, "Nama harus di isi","Peringatan",JOptionPane.INFORMATION_MESSAGE);
        return;
        }
        
        else if (TTL.isEmpty()){
        JOptionPane.showMessageDialog(null, "Tempat Tanggal Lahir harus di isi","Peringatan",JOptionPane.INFORMATION_MESSAGE);
        return;
        }
                
        else if(!btnPerempuan.isSelected()&&!btnLakiLaki.isSelected()){
        JOptionPane.showMessageDialog(null, "Gender harus di isi","Peringatan",JOptionPane.INFORMATION_MESSAGE);
        return;
        }
        
        else if (nomor.isEmpty()){
        JOptionPane.showMessageDialog(null, "Nomor Telepon harus di isi","Peringatan",JOptionPane.INFORMATION_MESSAGE);
        return;
        }
        
        else if (email.isEmpty()){
        JOptionPane.showMessageDialog(null, "Email harus di isi","Peringatan",JOptionPane.INFORMATION_MESSAGE);
        return;
        }
        
        else if (alamat.isEmpty()){
        JOptionPane.showMessageDialog(null, "Alamat harus di isi","Peringatan",JOptionPane.INFORMATION_MESSAGE);
        return;
        }
        
        new halaman2(nama,TTL,alamat,gender,nomor,email,pendidikan).setVisible(true);
        
        this.dispose();
        
    } else {
        JOptionPane.showMessageDialog(null, "Silakan Checklist Dulu", "GAGAL MENYIMPAN", JOptionPane.INFORMATION_MESSAGE);
        }          
    }//GEN-LAST:event_btnSimpanActionPerformed

