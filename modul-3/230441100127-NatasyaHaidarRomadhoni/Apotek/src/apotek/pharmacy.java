    private void btnInfoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInfoActionPerformed
       // Menghapus isi panel detail terlebih dahulu
        DetailObat.removeAll();
        
        // Mengecek checkbox yang dipilih dan menambahkan informasi yang sesuai
        if (CBFibramed.isSelected()) {
            addDetail("Fibramed", "Obat anti kolesterol yang termasuk golongan fibrat. Obat bekerja dengan menurunkan kadar trigliserida serum dan meningkatkan kadar lemak baik dalam darah. ", "300 mg per hari, dengan makanan.", "paracetamol.png");
        }
        if (CBMyLanta.isSelected()) {
            addDetail("MyLanta", "Obat ini menggunakan Quick Action Formula untuk mengatasi sakit maag dengan cepat sehingga kamu dapat kembali beraktivitas. ", "3x sehari setelah makan.", "mefenamat.png");
        }
        if (CBSanmolForte.isSelected()) {
            addDetail("SanmolForte", "Pilihan obat demam dewasa paling ampuh selanjutnya adalah Sanmol Forte yang memiliki kandungan paracetamol 650 mg.", "3-4 kali sehari sebanyak 1 tablet saja.", "lodia.png");
        }
        if (CBJamkhoSirup.isSelected()) {
            addDetail("JamkhoSirup", "Jamkho Sirup adalah herbal yang digunakan untuk menurunkan kadar kolesterol tinggi. ", "sebanyak 1 kali sehari 1 sendok makan, dan dapat dikonsumsi sebelum atau sesudah makan.", "Omeprazole.png");
        }
        if (CBMetformin.isSelected()) {
            addDetail("Metformin", "digunakan oleh penderita diabetes untuk menurunkan kadar gula dalam darah. Ini merupakan obat antidiabetes generik, yang bermanfaat untuk mengontrol dan menurunkan kadar gula darah pada penderita diabetes tipe-2. ", "Harus sesuai dengan petunjuk dokter.", "IbuProfen.png");
        }
        if (CBCimetidine.isSelected()) {
            addDetail("Cimetidine", "Cimetidine juga termasuk obat golongan inhibitor pompa proton yang digunakan untuk mengobati tukak duodenum dan tukak lambung yang ringan", "Sesuai petunjuk dokter.", "amoxicillin.png");
        }
        if (CBLipesco.isSelected()) {
            addDetail("Lipesco", "Lipesco merupakan obat yang banyak digunakan untuk membantu memelihara kesehatan penderita diabetes. ", "Setelah makan 1 kali sehari, sebanyak satu kaplet. ", "spasminal.png");
        }
        if (CBSumagesic.isSelected()) {
            addDetail("Sumagesic", "obat demam untuk dewasa pertama adalah Sumagesic yang merupakan obat dengan kandungan Paracetamol 600 mg.", "Dewasa sebanyak, 3-4 kali sehari.", "ketoconazole.png");
        }
        if (CBProsogan.isSelected()) {
            addDetail("Prosogan", "Salah satu obat untuk mengatasi gangguan asam lambung, termasuk tukak lambung dan penyakit refluks gastroesofageal (GERD). ", "Sesuai petunjuk dokter.", "Atorvastatin.png");
        }
        if (CBPanadol.isSelected()) {
            addDetail("Panadol", "Panadol adalah obat dengan kandungan paracetamol yang juga bisa menjadi obat demam dewasa paling ampuh.", "Sebanyak 1-2 kaplet, sebanyak 3-4 kali sehari.", "Allopurinol.png");
        }

        // Refresh panel agar perubahan terlihat
        DetailObat.revalidate();
        DetailObat.repaint();
    }

    private void addDetail(String namaObat, String detail, String caraPakai, String gambar) {
        // Menggunakan JTextField untuk nama obat
        JTextField tfNamaObat = new JTextField(namaObat);
        tfNamaObat.setEditable(false); // Supaya nama obat tidak bisa diubah
        tfNamaObat.setColumns(20); // Mengatur lebar field

        // Menggunakan JTextArea untuk detail obat dan cara pakai
        JTextArea taDetail = new JTextArea(5, 20); // 5 baris, 20 karakter per baris
        taDetail.setText("Detail Obat :" + detail +"\n Cara Pakai" + caraPakai);
        taDetail.setWrapStyleWord(true);
        taDetail.setLineWrap(true);
        taDetail.setEditable(false);
        taDetail.setOpaque(false); // Supaya tampil seperti JLabel


        // Menambahkan komponen ke panel detail
        DetailObat.add(new JLabel("Nama Obat:")); // Label untuk penjelasan
        DetailObat.add(tfNamaObat); // JTextField untuk nama obat
        DetailObat.add(taDetail); // JTextArea untuk detail dan cara pakai
     
    
    }//GEN-LAST:event_btnInfoActionPerformed

