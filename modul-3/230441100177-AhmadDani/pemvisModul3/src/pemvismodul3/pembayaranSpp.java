    private void btnHitungActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHitungActionPerformed
        String nama = tfNama.getText();
        String spp = tfSpp.getText();
        
        if(nama.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Nama Harus diisi", "Input Nama", JOptionPane.INFORMATION_MESSAGE);
            return;
        }
        
        if(spp.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Spp Harus diisi", "Input Spp", JOptionPane.INFORMATION_MESSAGE);
            return;
        }
        
        int intSpp;
        try { 
            intSpp = Integer.parseInt(spp);
        }catch (NumberFormatException e){ 
            JOptionPane.showMessageDialog(this, "Nilai spp harus berupa angka", "Input Spp", JOptionPane.INFORMATION_MESSAGE);
            return;
	}
        
        JCheckBox[] cbBulan = {blnJan, blnMar, blnApr, blnMei, blnJun, blnJul, blnAgu, blnSep, blnOkt, blnNov, blnDes};
        
        int jumlahSpp = 0;
        for (int i = 0; i < cbBulan.length; i++) {
            if (cbBulan[i].isSelected()) {
                jumlahSpp += intSpp;               
            }
        }
        
        tfJumlahSpp.setText(Integer.toString(jumlahSpp));
    }//GEN-LAST:event_btnHitungActionPerformed

