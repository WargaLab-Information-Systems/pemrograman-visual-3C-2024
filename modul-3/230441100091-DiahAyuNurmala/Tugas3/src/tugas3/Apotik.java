private void updateTotal() {
        int totalHarga = 0;
    int jumlahBeli = Integer.parseInt(tfJumlahBeli.getText());

    if (CBInsto.isSelected()) {
        totalHarga += 15000 * jumlahBeli;
    }
    if (CBherocynl.isSelected()) {
        totalHarga += 25000 * jumlahBeli;
    }
    if (CBforumen.isSelected()) {
        totalHarga += 15000 * jumlahBeli;
    }
    if (CBhotin.isSelected()) {
            totalHarga += 20000 * jumlahBeli;
    }
    if (CBdermatix.isSelected()) {
           totalHarga += 10000 * jumlahBeli;
        }
        if (CBAmoxicillin.isSelected()) {
            totalHarga += 10000 * jumlahBeli;
        }
        if (CBSplasminal.isSelected()) {
            totalHarga += 15000 * jumlahBeli;
        }
        if (CBKetoconazole.isSelected()) {
            totalHarga += 10000 * jumlahBeli;
        }
        if (CBIbuprofen.isSelected()) {
            totalHarga += 12000 * jumlahBeli;
        }
        if (CBAtorvastatin.isSelected()) {
            totalHarga += 10000 * jumlahBeli;
        }

        // Update JTextField dengan total harga
        tfTotalHarga.setText(String.valueOf(totalHarga));
    }
    
