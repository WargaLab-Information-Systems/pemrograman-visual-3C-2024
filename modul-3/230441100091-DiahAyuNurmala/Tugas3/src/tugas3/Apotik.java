    private void btnplusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnplusActionPerformed

        int jumlah = Integer.parseInt(tfJumlahBeli.getText());
        tfJumlahBeli.setText(String.valueOf(jumlah + 1)); // Tambah 1
        updateTotal(); // Update total harga

    }//GEN-LAST:event_btnplusActionPerformed

    private void btnminActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnminActionPerformed
         int jumlah = Integer.parseInt(tfJumlahBeli.getText());
                if (jumlah > 0) {
                    tfJumlahBeli.setText(String.valueOf(jumlah - 1)); // Kurangi 1
                    updateTotal(); // Update total harga
                }

    }//GEN-LAST:event_btnminActionPerformed

    private void btnBayarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBayarActionPerformed
                // Hitung total harga terlebih dahulu
    updateTotal(); // Memanggil fungsi yang sudah menghitung total harga
    
    // Ambil total harga yang sudah ter-update
    String totalHarga = tfTotalHarga.getText();
    
    // Tampilkan dialog yang menunjukkan total harga sebelum pembayaran berhasil
    JOptionPane.showMessageDialog(this, "Total Bayar: Rp " + totalHarga);
    
    // Reset total dan jumlah setelah pembayaran
    tfJumlahBeli.setText("1");
    tfTotalHarga.setText("0");
     
   
    }//GEN-LAST:event_btnBayarActionPerformed

