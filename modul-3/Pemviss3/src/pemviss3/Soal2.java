    private void BayarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BayarActionPerformed
        // TODO add your handling code here:
        int totalHarga = Integer.parseInt(Harga.getText().replace("Rp. ", ""));
        int cash = Integer.parseInt(Uang.getText());
        int kembalian = cash - totalHarga;
        if(cash < totalHarga){
            JOptionPane.showMessageDialog(this, "Uang anda kurang!!!!!", "PERINGATAN!!!", JOptionPane.WARNING_MESSAGE);
            return;
        }else if (cash > totalHarga){
            JOptionPane.showMessageDialog(this, "Pembayaran Suksesss\n " + "Kembalian = Rp. "+kembalian, "PERINGATAN!!!", JOptionPane.WARNING_MESSAGE);
        }else {
            JOptionPane.showMessageDialog(this, "Pembayaran Suksesss ","PERINGATAN!!!", JOptionPane.WARNING_MESSAGE);
        }
        StringBuilder PilihBulan = new StringBuilder();
        
        if(Januari.isSelected()){
            PilihBulan.append("Januari");
            Januari.setEnabled(false);
        }
        if(Februari.isSelected()){
            PilihBulan.append("februari");
            Februari.setEnabled(false);
        }
        if(Maret.isSelected()){
            PilihBulan.append("Maret, ");
            Maret.setEnabled(false);
        }
        if(April.isSelected()){
            PilihBulan.append("April, ");
            April.setEnabled(false);
        }
        if(Mei.isSelected()){
            PilihBulan.append("Mei, ");
            Mei.setEnabled(false);
        }
        if(Juni.isSelected()){
            PilihBulan.append("Juni, ");
            Juni.setEnabled(false);
        }
        if(Juli.isSelected()){
            PilihBulan.append("Juli, ");
            Juli.setEnabled(false);
        }
        if(Agustus.isSelected()){
            PilihBulan.append("Agustus, ");
            Agustus.setEnabled(false);
        }
        if(September.isSelected()){
            PilihBulan.append("September, ");
            September.setEnabled(false);
        }
        if(Oktober.isSelected()){
            PilihBulan.append("Oktober, ");
            Oktober.setEnabled(false);
        }
        if(November.isSelected()){
            PilihBulan.append("November, ");;
            November.setEnabled(false);
        }
        if(Desember.isSelected()){
            PilihBulan.append("Desember, ");
            Desember.setEnabled(false);
        }
        
        for(int i=0;i<=x;i++){
            if(Nama.getText()==Table.getValueAt(i, 0)){
                Table.setValueAt(Kelas.getSelectedItem(), i, 1);
                Table.setValueAt(PilihBulan.toString(), i, 2);
                Table.setValueAt(Harga.getText(), i, 3);
                Table.setValueAt("Lunas", i, 4);
            }else{
                Table.setValueAt(Nama.getText(), x, 0);
                Table.setValueAt(Kelas.getSelectedItem(), x, 1);
                Table.setValueAt(PilihBulan.toString(), x, 2);
                Table.setValueAt(Harga.getText(), x, 3);
                Table.setValueAt("Lunas", x, 4);
                x = x+1 ;
            }
        }
        
        Harga.setText("0");
        
    }//GEN-LAST:event_BayarActionPerformed

