      private void hitungKembalian() {
    try {
        // Ambil nilai total harga dan cash dari TextField
        int Total = Integer.parseInt(jTextFieldtotal.getText());
        int cash = Integer.parseInt(jTextField2cash.getText());

        
        if (cash >= Total) {
            int kembalian = cash - Total;
            jTextFieldKembalian.setText(String.valueOf(kembalian));  // Set nilai kembalian ke TextFieldkembali
        } else {
            jTextFieldKembalian.setText("Uang tidak mencukupi");
        }
    } catch (NumberFormatException e) { 
        jTextFieldKembalian.setText("");
    }
      }
    
