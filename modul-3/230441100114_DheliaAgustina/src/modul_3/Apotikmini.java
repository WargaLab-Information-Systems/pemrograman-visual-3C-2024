    private void totalActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_totalActionPerformed
        // TODO add your handling code here:
        if (!jCheckBoxbetadine.isSelected() && !jCheckBoxkayuputih.isSelected() && !jCheckBoxbodrex.isSelected() && 
        !jCheckBoxibuforen.isSelected() && !jCheckBoxmixagrip.isSelected() && !jCheckBoxoskadon.isSelected() && 
        !jCheckBoxpanadol.isSelected() && !jCheckBoxparacetamol.isSelected() && !jCheckBoxpromag.isSelected() && 
        !jCheckBoxvitaminc.isSelected()) {
        JOptionPane.showMessageDialog(this, "Anda belum memilih pesanan", "Peringatan", JOptionPane.WARNING_MESSAGE); 
        return;
}
        
        
        int Jumlah = Integer.parseInt(jTextFieldjumlah.getText());
        int  barang= 0;
        
        
        if (jCheckBoxbetadine.isSelected()){
            barang += 18000;
        }
        if (jCheckBoxkayuputih.isSelected()){
            barang += 67000;
        }
        if (jCheckBoxbodrex.isSelected()){
            barang += 3000;
        }
        if (jCheckBoxibuforen.isSelected()){
            barang += 5000;
        }
        if (jCheckBoxmixagrip.isSelected()){
            barang += 4000;
        }
        if (jCheckBoxoskadon.isSelected()){
            barang += 2000;
        }
        if (jCheckBoxpanadol.isSelected()){
            barang += 15000;
        }
        if (jCheckBoxparacetamol.isSelected()){
            barang += 4000;
        }
        if (jCheckBoxpromag.isSelected()){
            barang += 10000;
        }
        if (jCheckBoxvitaminc.isSelected()){
            barang += 45000;
        }
        
        int totalHarga = barang * Jumlah;
        
        jTextFieldtotal.setText(String.valueOf(totalHarga));
    }//GEN-LAST:event_totalActionPerformed

