    private void jButtonbayarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonbayarActionPerformed
//         TODO add your handling code here:
        double cash = Double.parseDouble(jTextFieldcash.getText());
        double totalPembayaran = Double.parseDouble(jTextFieldtotal.getText());

        if (cash >= totalPembayaran) {
            javax.swing.JOptionPane.showMessageDialog(null, "Pembayaran berhasil!");

            
            String nama = jTextFieldnama.getText();
            java.util.List<String> bulanDibayar = new java.util.ArrayList<>();
            if (januari.isSelected()) bulanDibayar.add("Januari");
            if (februari.isSelected()) bulanDibayar.add("Februari");
            if (maret.isSelected()) bulanDibayar.add("Maret");
            if (april.isSelected()) bulanDibayar.add("April");
            if (mei.isSelected()) bulanDibayar.add("Mei");
            if (juni.isSelected()) bulanDibayar.add("Juni");
            if (juli.isSelected()) bulanDibayar.add("Juli");
            if (agustus.isSelected()) bulanDibayar.add("Agustus");
            if (september.isSelected()) bulanDibayar.add("September");
            if (oktober.isSelected()) bulanDibayar.add("Oktober");
            if (november.isSelected()) bulanDibayar.add("November");
            if (desember.isSelected()) bulanDibayar.add("Desember");

            // Update tabel
            javax.swing.table.DefaultTableModel model = (javax.swing.table.DefaultTableModel) jTable1.getModel();

             model.setRowCount(0);
            
            // array
            String[] semuaBulan = {"Januari", "Februari", "Maret", "April", "Mei", "Juni", 
                                   "Juli", "Agustus", "September", "Oktober", "November", "Desember"};

            

            for (int i = 0; i < semuaBulan.length; i++) {
                String statusBulan = bulanDibayar.contains(semuaBulan[i]) ? "Lunas" : "Belum Bayar";
                 model.addRow(new Object[]{i + 1, nama, semuaBulan[i], statusBulan});
            }

        } else {
        javax.swing.JOptionPane.showMessageDialog(null, "Pembayaran gagal! Uang tidak mencukupi.");
        }

          

      

          
    }//GEN-LAST:event_jButtonbayarActionPerformed

