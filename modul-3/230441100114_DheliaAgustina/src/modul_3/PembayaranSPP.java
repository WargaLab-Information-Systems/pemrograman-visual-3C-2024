    private void HitungActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_HitungActionPerformed
        // TODO add your handling code here:
        String nama = jTextFieldnama.getText();
        double jumlahSPP = Double.parseDouble(jTextFieldspp.getText());
    
       
        java.util.List<String> bulanDibayar = new java.util.ArrayList<>();
    
        
        if (januari.isSelected()) bulanDibayar.add("Januari");
        if (februari.isSelected()) bulanDibayar.add("Februari");
        if (maret.isSelected()) bulanDibayar.add("Maret");
        if (april.isSelected()) bulanDibayar.add("April");
        if (mei.isSelected()) bulanDibayar.add("Mei");
        if (juni.isSelected()) bulanDibayar.add("Juni");
        if (juli.isSelected()) bulanDibayar.add("Juli");
        if (agustus.isSelected()) bulanDibayar.add("Agustus");
        if (september.isSelected()) bulanDibayar.add("September");
        if (oktober.isSelected()) bulanDibayar.add("Oktober");
        if (november.isSelected()) bulanDibayar.add("November");
        if (desember.isSelected()) bulanDibayar.add("Desember");

       
        int jumlahBulanBelumDibayar = 12 - bulanDibayar.size();

       
        double totalPembayaran = jumlahSPP * bulanDibayar.size();
    
        
        String status;
        if (bulanDibayar.size()== 12) {
            status = "Lunas";
        } else if (bulanDibayar.isEmpty()) {
            status = "Belum Bayar";
        } else {
            status = "Menunggak";
        }

        // menampilkan
        jTextFieldStatus.setText(status);
        jTextFieldtotal.setText(String.format("%.2f", totalPembayaran));
    }//GEN-LAST:event_HitungActionPerformed

