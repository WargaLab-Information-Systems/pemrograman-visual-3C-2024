    private void btnMulaiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnMulaiActionPerformed
        // TODO add your handling code here:
        if (User != null) { // Cek apakah pemain sudah memilih
                Acak();    // Acak pilihan komputer
                Uji();     // Tentukan hasil
            } else {
                JOptionPane.showMessageDialog(null, "Pilih Batu, Gunting, atau Kertas terlebih dahulu!", "Peringatan", JOptionPane.WARNING_MESSAGE);
            }
    }//GEN-LAST:event_btnMulaiActionPerformed
    
    private void Acak(){
        Random random = new Random();
        int BilAcak = random.nextInt(3); // Membuat bilangan acak 0 hingga 2
        if (BilAcak == 0){
            KompField.setText(Gunting);
            Komp = Gunting;
        }else if (BilAcak == 1) {
            KompField.setText(Kertas);
            Komp = Kertas;
        }else {
            KompField.setText(Batu);
            Komp = Batu;
        } 
    }
    
    private void Uji(){
        if (User.equals(Komp)) {
            JOptionPane.showMessageDialog(null, "Seri!", "Hasil", JOptionPane.INFORMATION_MESSAGE);
        } else if (User.equals(Kertas) && Komp.equals(Batu)) {
            JOptionPane.showMessageDialog(null, "Anda Menang!", "Hasil", JOptionPane.INFORMATION_MESSAGE);
        } else if (User.equals(Batu) && Komp.equals(Gunting)) {
            JOptionPane.showMessageDialog(null, "Anda Menang!", "Hasil", JOptionPane.INFORMATION_MESSAGE);
        } else if (User.equals(Gunting) && Komp.equals(Kertas)) {
            JOptionPane.showMessageDialog(null, "Anda Menang!", "Hasil", JOptionPane.INFORMATION_MESSAGE);
        } else {
            JOptionPane.showMessageDialog(null, "Anda Kalah!", "Hasil", JOptionPane.INFORMATION_MESSAGE);
        }
    }
    
    
