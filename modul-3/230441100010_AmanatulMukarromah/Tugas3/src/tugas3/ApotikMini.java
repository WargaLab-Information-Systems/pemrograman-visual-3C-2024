    private void btnPrintActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPrintActionPerformed
    double Harga = 0;
    String Obat = "";
    String Gejala = String.valueOf(ApotikMini.cmbpilih.getSelectedItem());
    String Nama = nama.getText();

    if (insuline.isSelected()) {
        Harga += 15000;
        Obat += "Ambroxol\n";
    }
    if (amlodipine.isSelected()) {
        Harga += 30000;
        Obat += "Amlodipine\n";
    }
    if (glibenclaime.isSelected()) {
        Harga += 15000;
        Obat += "glibenclaime\n";
    }
    if (fluoxe.isSelected()) {
        Harga += 4000;
        Obat += "fluoxe\n";
    }
    if (ciproflox.isSelected()) {
        Harga += 16000;
        Obat += "ciproflox\n";
    }
    if (paracetamol.isSelected()) {
        Harga += 5000;
        Obat += "Paracetamol\n";
    }
    if (ibuprofen.isSelected()) {
        Harga += 110000;
        Obat += "Ibuprofen\n";
    }
    if (amoxilin.isSelected()) {
        Harga += 35000;
        Obat += "amoxilin\n";
    }
    if (sertraline.isSelected()) {
        Harga += 5000;
        Obat += "Oralit\n";
    }
    if (lasortan.isSelected()) {
        Harga += 5000;
        Obat += "Oseltamivir\n";
    }
    if (Gejala.isEmpty() || Obat.isEmpty() || Nama.isEmpty()) {
        JOptionPane.showMessageDialog(this,"Anda belum mengisi form dengan benar, tolong diisi secara keseluruhan","SILAHKAN MENGISI FORM !", JOptionPane.WARNING_MESSAGE);
    }
    else {hasil.setText("Nama = "+ nama.getText() +" || Gejala = "+ Gejala + " || Obat =" + Obat + " || Total Harga : Rp. " + Harga);
    }
    }//GEN-LAST:event_btnPrintActionPerformed

