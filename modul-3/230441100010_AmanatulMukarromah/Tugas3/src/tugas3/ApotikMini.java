    private void cmbpilihActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmbpilihActionPerformed
    String Gejala=  String.valueOf(ApotikMini.cmbpilih.getSelectedItem());
        if(Gejala.equals("Demam")){
        JOptionPane.showMessageDialog(this,"Obat untuk demam adalah Paracetamol dan ibu profen","DISCLAIMER !!!", JOptionPane.INFORMATION_MESSAGE);
        } else if(Gejala.equals("Nyeri")){
        JOptionPane.showMessageDialog(this,"Obat untuk hipertensi adalah Amoxilin dan Ciprolax ","DISCLAIMER !!!", JOptionPane.INFORMATION_MESSAGE);  
        }else if(Gejala.equals("Hipertensi")){
        JOptionPane.showMessageDialog(this,"Obat untuk diare adalah Amlodipine dan Lasortan","DISCLAIMER !!!", JOptionPane.INFORMATION_MESSAGE);  
        }else if(Gejala.equals("Diabetes")){
        JOptionPane.showMessageDialog(this,"Obat untuk flu adalah Insulin dan Glibanclaime","DISCLAIMER !!!", JOptionPane.INFORMATION_MESSAGE);  
        }else if(Gejala.equals("Depresi")){
        JOptionPane.showMessageDialog(this,"Obat untuk batuk adalah Sertraline dan fluoxe","DISCLAIMER !!!", JOptionPane.INFORMATION_MESSAGE);  
        }
    }//GEN-LAST:event_cmbpilihActionPerformed

