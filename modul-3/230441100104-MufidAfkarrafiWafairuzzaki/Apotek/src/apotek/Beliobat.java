public void hitungkembalian() {
    try {
        int totalHarga = Integer.parseInt(txttotal.getText());
        int cash = Integer.parseInt(txtcash.getText());
        String kembalian;

        if (cash >= totalHarga) {
            kembalian = String.valueOf(cash - totalHarga); 
        } else {
            kembalian = "Uang Kurang"; 
        }

        txtkembali.setText(kembalian); 
    } catch (NumberFormatException ex) {
        txtkembali.setText(""); 
    }
}

