    private void btnBeliActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBeliActionPerformed
        // TODO add your handling code here:
        if(teksTotalHarga.getText().isEmpty()){
            JOptionPane.showMessageDialog(null,"Silakan Buat Pesanan terlebih Dahulu","Peringatan",JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        String nama = teksNama.getText();
        String noHP = teksNoTelepon.getText();
        
        if (nama.isEmpty()||noHP.isEmpty()){
            JOptionPane.showMessageDialog(null,"Nama/No telpon belum Di isi","Peringatan",JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        int pilihan = JOptionPane.showConfirmDialog(null,"Lanjutkan Pembayaran ?","Awas",JOptionPane.YES_NO_OPTION);
        if(pilihan == JOptionPane.YES_OPTION){
        JOptionPane.showMessageDialog(null,"Nama : " + teksNama.getText()+"\n No Telepon : "+ teksNoTelepon.getText()+"\nJumlah yang harus dibayar : "+teksTotalHarga.getText(),"Perhatian",JOptionPane.INFORMATION_MESSAGE);
        cbParacetamol.setSelected(false);
        cbFlutanol.setSelected(false);
        cbFirstAid.setSelected(false);
        cbMedkit.setSelected(false);
        cbCinovir.setSelected(false);
        cbHealingSpell.setSelected(false);
        cbCooling5.setSelected(false);
        cbZiloric.setSelected(false);
        cbEpisol.setSelected(false);
        cbDiapet.setSelected(false);
        
        teksTotalHarga.setText("");
        teksDiskon.setText("");
        
        teksNama.setText("");
        teksNoTelepon.setText("");
        }
    }//GEN-LAST:event_btnBeliActionPerformed
        

