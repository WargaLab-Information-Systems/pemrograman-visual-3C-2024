    private void btnBuatPesananActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuatPesananActionPerformed
        // TODO add your handling code here:
        int total_harga = 0;
        boolean status = false;
        
        if(cbParacetamol.isSelected()){
        total_harga += 5000;
        status = true;
        }
        if(cbFlutanol.isSelected()){
        total_harga +=8000;
        status = true;
        }
        if(cbMedkit.isSelected()){
        total_harga += 12000;
        status = true;
        }
        if (cbFirstAid.isSelected()){
        total_harga += 50000;
        status = true;
        }
        if(cbCinovir.isSelected()){
        total_harga +=25000;
        status = true;
        }
        if(cbHealingSpell.isSelected()){
        total_harga += 200000;
        status = true;
        }
        if(cbCooling5.isSelected()){
        total_harga +=18000;
        status = true;
        }
        if(cbZiloric.isSelected()){
        total_harga += 40000;
        status = true;
        }
        if(cbEpisol.isSelected()){
        total_harga += 10000;
        status = true;
        }
        if(cbDiapet.isSelected()){
        total_harga += 15000;
        status = true;
        }
        
        double diskon = 0;
        if(!status){
            JOptionPane.showMessageDialog(null,"Pilih Setidaknya 1 Obat","",JOptionPane.WARNING_MESSAGE);
        }else{
        if(total_harga>100000){
        diskon += 0.05*total_harga;
            }
        }       
        
        teksTotalHarga.setText(String.valueOf(total_harga));
        teksDiskon.setText(String.valueOf(diskon));
        
    }//GEN-LAST:event_btnBuatPesananActionPerformed

