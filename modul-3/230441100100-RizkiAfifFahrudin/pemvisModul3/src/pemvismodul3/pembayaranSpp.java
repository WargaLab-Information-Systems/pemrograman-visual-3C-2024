    private void cbNovemberActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cbNovemberActionPerformed
        // TODO add your handling code here:\
          
        if(cbNovember.isSelected() && !teksSPP.getText().isEmpty()){
        tagihan+=Integer.parseInt(teksSPP.getText());
        
        }
        if(cbNovember.isSelected()== false && !teksSPP.getText().isEmpty()){
        tagihan-=Integer.parseInt(teksSPP.getText());
        }
    }//GEN-LAST:event_cbNovemberActionPerformed

    private void btnBayarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBayarActionPerformed
        // TODO add your handling code here:
        if(tagihan==0){
        JOptionPane.showConfirmDialog(null,"","",JOptionPane.WARNING_MESSAGE);
        return;
        }

        for(int i = 0; i<bulan.length;i++){
            if(bulan[i].isSelected()){
            status[i] = true;
            bulan[i].setEnabled(false);
            }
        }
        boolean cekLunas = true;  
        for (int i = 0; i < bulan.length; i++) {
        if (!status[i]) {
            cekLunas = false;  
            break;  
            }
        }   

        if (cekLunas) {
            teksStatus.setText("Lunas");
        } else {
            teksStatus.setText("Menunggak");
        }
        JOptionPane.showMessageDialog(null,"Pembayaran berhasil dilakukan","ALAMAK!!!",JOptionPane.INFORMATION_MESSAGE);
    
        tagihan =0;
        teksTotal.setText(String.valueOf(tagihan));
    }//GEN-LAST:event_btnBayarActionPerformed

