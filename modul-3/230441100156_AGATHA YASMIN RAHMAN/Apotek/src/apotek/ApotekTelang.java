    private void jButton11ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton11ActionPerformed
        // TODO add your handling code here:
    String gejala = (String) cbxpenyakit.getSelectedItem();  // Mendapatkan gejala yang dipilih
    String deskripsi = "";  // Inisialisasi deskripsi

    if (gejala.equals("Demam")) {
        deskripsi = "Obat yang dapat digunakan: \n" +
                    "1. Ibuproven: Untuk mengatasi demam \n"
                + "dan sakit kepala ringan.";
    } else if (gejala.equals("Jerawat")) {
        deskripsi = "Obat yang dapat digunakan: \n" +
                    "1. Medi klin: Mengobati bakteri jerawat \n"
                + "seperti Panu atau \n"
                + "infeksi kulit.";
    } else if (gejala.equals("Sakit Gigi")) {
        deskripsi = "Obat yang dapat digunakan: \n" +
                    "1. Amoxcillint: Untuk mengatasi nyeri \n"
                + "dan Sakit Gigi.";
    } else if (gejala.equals("Alergi")) {
        deskripsi = "Obat yang dapat digunakan: \n" +
                    "1. CTM: Mengatasi gejala alergi \n"
                + "seperti bersin, pilek, dan gatal-gatal.";
    } else if (gejala.equals("Diare")) {
        deskripsi = "Obat yang dapat digunakan: \n" +
                    "1. Siladex: Untuk Diare dan pencernaan \n"
                + "infeksi bakteri atau virus.";
    } else if (gejala.equals("Maag")) {
        deskripsi = "Obat yang dapat digunakan: \n" +
                    "1. Mylanta: Mengurangi asam lambung \n"
                + "Mulas,Asam lambung , dan Maag.";
    } else if (gejala.equals("Demam")) {
        deskripsi = "Obat yang dapat digunakan: \n" +
                    "1. Paracetamol: Untuk menurunkan demam \n"
                + "darah tinggi (hipertensi).";
    } else if (gejala.equals("Ginjal")) {
        deskripsi = "Obat yang dapat digunakan: \n" +
                    "1. Dexamethasone: Untuk mengatasi penyakit ginjal \n"
                + "akut atau kronis.";
    } else if (gejala.equals("OBAT CACINGAN")) {
        deskripsi = "Obat yang dapat digunakan: \n" +
                    "1. Mebendazole: Untuk infeksi cacing gelang. \n"
                + "cacing kremi, dan cacing pita..";
    } else if (gejala.equals("Kolesterol")) {
        deskripsi = "Obat yang dapat digunakan: \n" +
                    "1. Hemaviton Cardio: Untuk mengatasi mengurangi penyerapan kolesterol jahat dalam tubuh. \n"
                + "Biasanya digunakan untuk kolesterol";
    }

    txtdeskripsi.setText(deskripsi);

    }//GEN-LAST:event_jButton11ActionPerformed

