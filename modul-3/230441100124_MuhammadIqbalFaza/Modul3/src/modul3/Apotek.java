    private void BtnBuatPesananActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnBuatPesananActionPerformed
        // TODO add your handling code here:
        // Ambil data dari input
        String namaPemesan = NamaPemesanField.getText();
        String gejala = (String) jComboBox1.getSelectedItem();
        String obatDipilih = ""; // Untuk menyimpan daftar obat yang dipilih
        int totalHarga = 0;

        // Cek jika nama pemesan kosong
        if (namaPemesan.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Nama pemesan tidak boleh kosong!", "Error", JOptionPane.ERROR_MESSAGE);
            return; // Menghentikan eksekusi jika nama kosong
        }

        // Ambil obat yang dipilih
        if (Paracetamol.isSelected()) {
            obatDipilih += "Paracetamol, ";
            totalHarga += 5000;
        }
        if (IbuProfen.isSelected()) {
            obatDipilih += "Ibuprofen, ";
            totalHarga += 7000;
        }
        if (Panadol.isSelected()) {
            obatDipilih += "Panadol, ";
            totalHarga += 8000;
        }
        if (Bodrex.isSelected()) {
            obatDipilih += "Bodrex, ";
            totalHarga += 6000;
        }
        if (Mixagrip.isSelected()) {
            obatDipilih += "Mixagrip, ";
            totalHarga += 5000;
        }
        if (Neozep.isSelected()) {
            obatDipilih += "Neozep, ";
            totalHarga += 6500;
        }
        if (OBHCombi.isSelected()) {
            obatDipilih += "OBH Combi, ";
            totalHarga += 5500;
        }
        if (Siladex.isSelected()) {
            obatDipilih += "Siladex, ";
            totalHarga += 6000;
        }
        if (Promag.isSelected()) {
            obatDipilih += "Promag, ";
            totalHarga += 4500;
        }
        if (Mylanta.isSelected()) {
            obatDipilih += "Mylanta, ";
            totalHarga += 5000;
        }

        // Hapus koma terakhir jika ada
        if (!obatDipilih.isEmpty()) {
            obatDipilih = obatDipilih.substring(0, obatDipilih.length() - 2);
        } else {
            JOptionPane.showMessageDialog(this, "Pilih setidaknya satu obat!", "Error", JOptionPane.ERROR_MESSAGE);
            return; // Menghentikan eksekusi jika tidak ada obat yang dipilih
        }

        // Tampilkan konfirmasi pesanan
        String pesan = "Pesanan Anda:\n"
                + "Nama Pemesan: " + namaPemesan + "\n"
                + "Gejala: " + gejala + "\n"
                + "Obat yang Dipilih: " + obatDipilih + "\n"
                + "Total Harga: Rp " + totalHarga;

        JOptionPane.showMessageDialog(this, pesan, "Pesanan Berhasil", JOptionPane.INFORMATION_MESSAGE);
        clearAllCheckboxes();
        NamaPemesanField.setText("");
        totalHargaField.setText("");
    }//GEN-LAST:event_BtnBuatPesananActionPerformed

