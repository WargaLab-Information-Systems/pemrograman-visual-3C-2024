    // Fungsi untuk memperbarui total harga
    private void updateTotalHarga() {
        totalHarga = 0; // Reset total harga

        // Tambah harga obat jika checkbox tercentang
        if (Paracetamol.isSelected()) totalHarga += 5000;
        if (IbuProfen.isSelected()) totalHarga += 7000;
        if (Panadol.isSelected()) totalHarga += 8000;
        if (Bodrex.isSelected()) totalHarga += 6000;
        if (Mixagrip.isSelected()) totalHarga += 5000;
        if (Neozep.isSelected()) totalHarga += 6500;
        if (OBHCombi.isSelected()) totalHarga += 5500;
        if (Siladex.isSelected()) totalHarga += 6000;
        if (Promag.isSelected()) totalHarga += 4500;
        if (Mylanta.isSelected()) totalHarga += 5000;

        // Tampilkan total harga di kolom yang sesuai
        totalHargaField.setText("Rp " + totalHarga);
    }
    
