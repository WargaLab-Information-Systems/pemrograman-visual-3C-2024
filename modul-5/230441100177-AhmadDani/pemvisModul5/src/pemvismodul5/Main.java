    //Load Data
    private void loadData() {
      model.setRowCount(0);

      try {
          String sql = "SELECT * FROM mahasiswa";
          PreparedStatement ps = conn.prepareStatement(sql);
          ResultSet rs = ps.executeQuery();
          while (rs.next()) {
             // Menambahkan baris ke dalam model tabel
             model.addRow(new Object[]{
             rs.getInt("id"),
             rs.getString("nama"),
             rs.getString("nim"),
             rs.getString("prodi")
           });
          }
      } catch (SQLException e) {
         System.out.println("Error Save Data" + e.getMessage());
       }
    }
    //end Load Data
    
    //Simpan Data
    private void saveData() {
      try{
         String sql = "INSERT INTO mahasiswa (nama, nim, prodi) VALUES (?, ?, ?)";
         PreparedStatement ps = conn.prepareStatement(sql);
         ps.setString(1, tf_nama.getText());
         ps.setString(2, tf_nim.getText());
         ps.setString(3, prodi.getSelectedItem().toString());
         ps.executeUpdate();
         JOptionPane.showMessageDialog(this, "Data saved successfully");
         loadData();
       } catch (SQLException e) {
         System.out.println("Error Save Data" + e.getMessage());
       }
     }
    //End Simpan Data
    
    //Update Data
    private void updateData() {
      try {
          String sql = "UPDATE mahasiswa SET nama = ?, nim = ? WHERE id = ?";
          PreparedStatement ps = conn.prepareStatement(sql);
          ps.setString(1, tf_nama.getText());
          ps.setString(2, tf_nim.getText());
          ps.setInt(3, Integer.parseInt(tf_id.getText()));
          ps.executeUpdate();
          JOptionPane.showMessageDialog(this, "Data updated successfully");
          loadData();
      }  catch (SQLException e) {
          System.out.println("Error Save Data" + e.getMessage());
      }
     }
    //End Simpan Data
    
    //Delete Data
    private void deleteData() {
     try  {
          String sql = "DELETE FROM mahasiswa WHERE id = ?";
          PreparedStatement ps = conn.prepareStatement(sql);
          ps.setInt(1, Integer.parseInt(tf_id.getText()));
          ps.executeUpdate();
          JOptionPane.showMessageDialog(this, "Data deleted successfully");
          loadData();
     } catch (SQLException e) {
          System.out.println("Error Save Data" + e.getMessage());
      }
    }
